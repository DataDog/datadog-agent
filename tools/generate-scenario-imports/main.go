// generate-scenario-imports regenerates test/e2e-framework/run/scenarios_import_gen.go by
// scanning for test/new-e2e/tests/*/scenario.go files and emitting a blank import for each
// matching package so its init() fires in the run binary.
//
// Run via: go generate ./test/e2e-framework/run/
package main

import (
	"fmt"
	"os"
	"path/filepath"
	"sort"
	"strings"
	"text/template"
)

const modulePrefix = "github.com/DataDog/datadog-agent"

const outputTemplate = `// Unless explicitly stated otherwise all files in this repository are licensed
// under the Apache License Version 2.0.
// This product includes software developed at Datadog (https://www.datadoghq.com/).
// Copyright 2025-present Datadog, Inc.

// Code generated by "go generate ./test/new-e2e/run/". DO NOT EDIT.

package main

import (
{{- range .}}
	_ "{{.}}"
{{- end}}
)
`

func main() {
	repoRoot, err := findRepoRoot()
	if err != nil {
		fmt.Fprintf(os.Stderr, "error: %v\n", err)
		os.Exit(1)
	}

	pattern := filepath.Join(repoRoot, "test", "new-e2e", "tests", "*", "scenario.go")
	matches, err := filepath.Glob(pattern)
	if err != nil {
		fmt.Fprintf(os.Stderr, "glob error: %v\n", err)
		os.Exit(1)
	}

	var imports []string
	for _, match := range matches {
		rel, err := filepath.Rel(repoRoot, filepath.Dir(match))
		if err != nil {
			fmt.Fprintf(os.Stderr, "rel error: %v\n", err)
			os.Exit(1)
		}
		imports = append(imports, modulePrefix+"/"+filepath.ToSlash(rel))
	}
	sort.Strings(imports)

	outPath := filepath.Join(repoRoot, "test", "new-e2e", "run", "scenarios_import_gen.go")

	// Write to a temp file first so a partial render never corrupts the output.
	tmp, err := os.CreateTemp(filepath.Dir(outPath), "scenarios_import_gen_*.go.tmp")
	if err != nil {
		fmt.Fprintf(os.Stderr, "create temp error: %v\n", err)
		os.Exit(1)
	}
	tmpPath := tmp.Name()

	tmpl := template.Must(template.New("imports").Parse(outputTemplate))
	if err := tmpl.Execute(tmp, imports); err != nil {
		tmp.Close()
		os.Remove(tmpPath)
		fmt.Fprintf(os.Stderr, "template error: %v\n", err)
		os.Exit(1)
	}
	if err := tmp.Close(); err != nil {
		os.Remove(tmpPath)
		fmt.Fprintf(os.Stderr, "close error: %v\n", err)
		os.Exit(1)
	}
	if err := os.Rename(tmpPath, outPath); err != nil {
		if rmErr := os.Remove(tmpPath); rmErr != nil {
			fmt.Fprintf(os.Stderr, "cleanup error (stale tmp file %s): %v\n", tmpPath, rmErr)
		}
		fmt.Fprintf(os.Stderr, "rename error: %v\n", err)
		os.Exit(1)
	}

	fmt.Printf("wrote %s with %d import(s):\n", outPath, len(imports))
	for _, imp := range imports {
		fmt.Printf("  _ %q\n", imp)
	}
}

func findRepoRoot() (string, error) {
	dir, err := os.Getwd()
	if err != nil {
		return "", err
	}
	for {
		if _, err := os.Stat(filepath.Join(dir, "go.mod")); err == nil {
			content, err := os.ReadFile(filepath.Join(dir, "go.mod"))
			if err == nil && strings.Contains(string(content), "module "+modulePrefix+"\n") {
				return dir, nil
			}
		}
		parent := filepath.Dir(dir)
		if parent == dir {
			return "", fmt.Errorf("could not find repo root with module %q", modulePrefix)
		}
		dir = parent
	}
}
