// Go log callback for the Rust library backend.
// Registers a Go function as the log::Log backend so that Rust log::debug!() etc.
// end up in the agent's unified logging system instead of being silently dropped.

//go:build dd_discovery_rust && cgo

package module

/*
#cgo CFLAGS: -I${SRCDIR}/rust/include
#include "dd_discovery.h"

// Forward declaration of the exported Go callback so the C compiler can form
// a function pointer of the correct type for dd_discovery_init_logger.
// The definition is generated by CGo from the //export annotation below.
extern void goDiscoveryLogCallback(unsigned int level, const char* msg, size_t msg_len);
*/
import "C"

import (
	"math"
	"unsafe"

	"github.com/DataDog/datadog-agent/pkg/util/log"
)

// goDiscoveryLogCallback is called by the Rust library for each log record.
// It bridges Rust log levels to the agent's pkg/util/log functions.
//
//export goDiscoveryLogCallback
func goDiscoveryLogCallback(level C.uint, msg *C.char, msgLen C.size_t) {
	if msg == nil || msgLen == 0 || msgLen > math.MaxInt32 {
		return
	}
	message := C.GoStringN(msg, C.int(msgLen))
	switch level {
	case 1:
		_ = log.Errorf("[dd_discovery] %s", message)
	case 2:
		_ = log.Warnf("[dd_discovery] %s", message)
	case 3:
		log.Infof("[dd_discovery] %s", message)
	case 4:
		log.Debugf("[dd_discovery] %s", message)
	default: // 5 = Trace (and any unknown level)
		log.Tracef("[dd_discovery] %s", message)
	}
}

// initRustLogger registers goDiscoveryLogCallback as the Rust log backend.
// Safe to call multiple times — dd_discovery_init_logger is a no-op after the
// first successful registration (guarded by an AtomicBool on the Rust side).
func initRustLogger() {
	C.dd_discovery_init_logger(
		// SAFETY: goDiscoveryLogCallback is a statically-compiled //export function
		// that lives for the entire process lifetime.
		(C.dd_log_fn)(unsafe.Pointer(C.goDiscoveryLogCallback)),
		4, // LevelFilter::Debug — Trace is filtered out in Rust; Go filters further.
	)
}
