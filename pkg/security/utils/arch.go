package utils

import "runtime"

// RuntimeArch returns the arch as will be visible in CWS events and security profiles
func RuntimeArch() string {
	switch runtime.GOARCH {
	case "amd64":
		return "x64"
	case "arm64":
		return "arm64"
	default:
		return runtime.GOARCH
	}
}
