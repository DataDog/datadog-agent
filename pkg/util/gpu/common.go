// Package gpu provides utilities for interacting with GPU resources.
package gpu

import (
	"regexp"
	"strings"
)

// ResourceGPU represents a GPU resource
type ResourceGPU string

// Resource name prefixes
const (
	// GpuNvidiaGeneric is the resource name for a generic NVIDIA GPU
	GpuNvidiaGeneric ResourceGPU = "nvidia.com/gpu"
	// GpuAMD is the resource name for an AMD GPU
	GpuAMD ResourceGPU = "amd.com/gpu"
	// GpuIntelXe is the resource name for an Intel Xe GPU
	GpuIntelXe ResourceGPU = "gpu.intel.com/xe"
	// GpuInteli915 is the resource name for an Intel i915 GPU
	GpuInteli915 ResourceGPU = "gpu.intel.com/i915"
	// GpuNvidiaMigPrefix is the prefix for NVIDIA MIG GPU resources
	GpuNvidiaMigPrefix ResourceGPU = "nvidia.com/mig"
)

// longToShortGPUName maps a GPU resource to a simplified name
var longToShortGPUName = map[ResourceGPU]string{
	GpuNvidiaGeneric: "nvidia",
	GpuAMD:           "amd",
	GpuIntelXe:       "intel",
	GpuInteli915:     "intel",
}

// this regex matches device names from NVML and extracts the GPU type. For example, from "nvidia_a100-80gb" it will extract "a100". The groups are as follows:
// 1. The optional prefix "nvidia" or "tesla" (T4 GPUs are named "tesla_t4" despite being NVIDIA GPUs)
// 2. The optional prefix "geforce_" which we ignore
// 3. The optional prefix "rtx_pro_" or "rtx_", which we use it as it's part of the GPU type
// 4. The GPU type, which is the next alphanumeric part of the device name. Anything behind it (such as the memory size or whether it's PCI or SXM) is ignored.
var gpuTypeRegex = regexp.MustCompile(`^(?:nvidia|tesla)_(?:geforce_)?(rtx_pro_|rtx_)?([a-z\d]+)`)
var gpuNameSeparatorRegex = regexp.MustCompile(`[^a-z\d]+`)

// ExtractSimpleGPUName returns a simplified GPU name.
// If the resource is not recognized, the second return value is false.
func ExtractSimpleGPUName(gpuName ResourceGPU) (string, bool) {
	val, ok := longToShortGPUName[gpuName]
	if ok {
		return val, true
	}

	// More complex cases (eg. nvidia.com/mig-3g.20gb => nvidia)
	switch {
	case strings.HasPrefix(string(gpuName), string(GpuNvidiaMigPrefix)):
		return "nvidia", true
	}

	// Not a GPU resource (or not recognized)
	return "", false
}

// IsNvidiaKubernetesResource returns true if the resource name is a Kubernetes resource
// for an NVIDIA GPU, either a generic GPU or a MIG GPU.
func IsNvidiaKubernetesResource(resourceName string) bool {
	return strings.HasPrefix(resourceName, string(GpuNvidiaMigPrefix)) ||
		resourceName == string(GpuNvidiaGeneric)
}

// ExtractGPUType returns the normalized GPU model type from a device name.
func ExtractGPUType(deviceName string) string {
	if deviceName == "" {
		return ""
	}

	// Normalize case/whitespace and remove leading/trailing noise so regex matching is stable.
	normalizedName := strings.ToLower(strings.TrimSpace(deviceName))
	// Collapse any non-alphanumeric separators (spaces, dashes, quotes, punctuation) into underscores.
	normalizedName = gpuNameSeparatorRegex.ReplaceAllString(normalizedName, "_")
	// Trim underscores added by leading/trailing separators.
	normalizedName = strings.Trim(normalizedName, "_")

	// Extract the optional RTX prefix and the GPU model token.
	matches := gpuTypeRegex.FindStringSubmatch(normalizedName)
	if len(matches) == 0 {
		return ""
	}

	// Combine optional RTX prefix with the model token (e.g., rtx_3090).
	return matches[1] + matches[2]
}

// NormalizeGPUDeviceName normalizes a GPU device name by converting it to lowercase and replacing spaces with underscores.
func NormalizeGPUDeviceName(device string) string {
	return strings.ToLower(strings.ReplaceAll(device, " ", "_"))
}

var replacementsWithDashes = []struct {
	regex       *regexp.Regexp
	replacement string
}{
	{regex: regexp.MustCompile(`([AL]\d+) (\d+Q)`), replacement: "$1-$2"},
	{regex: regexp.MustCompile(`A100 SXM4 (\d+)GB`), replacement: "A100-SXM4-${1}GB"},
	{regex: regexp.MustCompile(`V100 (\d+)GB`), replacement: "V100-${1}GB"},
}

// GFDLabelToGPUDeviceName converts a GPU Feature Discovery label to a GPU Device name as generated by NVML
func GFDLabelToGPUDeviceName(label string) string {
	// GFD takes NVML device names and converts them to label-safe values with dashes instead of spaces.
	// We need to revert that back to spaces for the GPU Device name, because NormalizeGPUDeviceName replaces spaces with underscores.

	// Do a generic replacement of all dashes by spaces. Most of the time, NVML returns
	// components separated by spaces, not dashes. So this covers most cases.
	label = strings.ReplaceAll(label, "-", " ")

	// However, we know that some strings are always separated by dashes. So we replace them back manually
	for _, replacement := range replacementsWithDashes {
		label = replacement.regex.ReplaceAllString(label, replacement.replacement)
	}

	return label
}
