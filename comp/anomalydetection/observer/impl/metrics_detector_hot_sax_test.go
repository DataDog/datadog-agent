// TestHotSAX_NoFireAfterStableLevelShift verifies the canonical Hot-SAX
// promise: a sustained level shift collapses to the same SAX word as the
// pre-shift stable phase (because z-normalization is computed within each
// window). Once the window has fully stabilized at the new level, no further
// fires may occur.
//
// DEVIATION FROM PLAN: the plan asked for "96 warmup + step + 30 post-shift"
// (= 127 ticks). With hotSaxMinFill=16 and hotSaxScoreEveryNth=4, the
// dictionary needs 16 scoring ticks (= 64 ticks past warmup) before any score
// is even computed — i.e. tick ~160. A 127-tick test never reaches the
// scoring phase, so the assertion would pass trivially. We extend the
// post-shift phase to 400 points (total ~496 ticks, ~100 scoring evaluations
// after MinFill is met), and assert NO fires past the post-shift
// stabilization point — the contract that genuinely tests the z-norm
// collapse property.
func TestHotSAX_NoFireAfterStableLevelShift(t *testing.T) {
	d := testHotSAXDetector()
	storage := newTimeSeriesStorage()

	rng := rand.New(rand.NewSource(1)) //nolint:gosec // deterministic test seed

	// Warmup at level 50.
	ts := addHotSAXPoints(storage, hotSaxWarmupPoints, 1, func(_ int) float64 {
		return 50 + rng.NormFloat64()
	})
	// Single-tick step from 50 to 80.
	stepTS := ts
	storage.Add("ns", "metric", 80, stepTS, nil)
	ts++
	// Long post-shift stable phase at level 80 — long enough that the dict
	// fills past MinFill AND the window has been fully stable for many
	// scoring ticks.
	postShiftN := 400
	ts = addHotSAXPoints(storage, postShiftN, ts, func(_ int) float64 {
		return 80 + rng.NormFloat64()
	})

	// "Stabilization point": the tick where the window first contains only
	// post-shift values. The window is hotSaxWindowSize points; the step
	// happened at stepTS. So the window is fully post-shift starting at
	// stepTS + hotSaxWindowSize. We assert NO fire at or after this point.
	stableFromTS := stepTS + int64(hotSaxWindowSize)

	result := d.Detect(storage, ts-1)

	for _, a := range result.Anomalies {
		assert.Lessf(t, a.Timestamp, stableFromTS,
			"unexpected fire after window stabilized at the new level (ts=%d, stableFromTS=%d, score=%v)",
			a.Timestamp, stableFromTS, a.Score)
	}
}

// TestHotSAX_FiresOnTransientSpike verifies that a localized transient spike
// produces a discord against a regular periodic baseline. The spike creates
// a SAX word that does not match any phase-shift of the dictionary's
// periodic words, driving MINDIST above the threshold.
//
// DEVIATION FROM PLAN: the plan called for "96 warmup + 5-point burst +
// N(50,1) recovery". The plan's N(50,1) baseline does NOT reliably surface
// the spike: random Gaussian noise generates a broad range of SAX words
// covering most of [0, alphabet-1]^WordLen, and the SAX alphabet is small
// enough (4) that almost any "discord" word has a stable-noise neighbor
// within ±1 letter at every position — i.e. the noise dictionary already
// covers the burst's word, so MINDIST collapses to 0. Hot-SAX is
// specifically designed to find discords against REGULAR patterns (Keogh
// 2005 §1: "We show that this representation allows the implementation of
// the first symbolic [...] discord algorithm, which can find anomalies in
// massive time series."). We use a clean period-16 sinusoid as the regular
// baseline; the dictionary fills with the ~4 distinct phase-shifts of that
// sinusoid and the burst word stands out cleanly. Sizing is also extended
// (200 + 5 + 100 instead of 96 + 5 + 30) so the dictionary reaches
// hotSaxMinFill before the spike arrives.
func TestHotSAX_FiresOnTransientSpike(t *testing.T) {
	d := testHotSAXDetector()
	storage := newTimeSeriesStorage()

	const (
		stablePreN  = 200
		burstN      = 5
		stablePostN = 100
		spike       = 100.0
	)

	periodic := func(i int) float64 {
		return math.Sin(2 * math.Pi * float64(i) / 16.0)
	}

	// Pre-spike periodic phase (covers warmup + dict fill).
	ts := addHotSAXPoints(storage, stablePreN, 1, func(i int) float64 {
		return periodic(i)
	})
	burstStart := ts
	ts = addHotSAXPoints(storage, burstN, ts, func(_ int) float64 {
		return spike
	})
	burstEnd := ts // exclusive
	ts = addHotSAXPoints(storage, stablePostN, ts, func(i int) float64 {
		return periodic(stablePreN + burstN + i)
	})

	result := d.Detect(storage, ts-1)

	// Look for a fire while the burst is still inside a scoring window —
	// i.e. between burstStart and burstEnd + hotSaxWindowSize (after which
	// the window is fully past the burst again).
	burstWindowEnd := burstEnd + int64(hotSaxWindowSize)
	var fires []observer.Anomaly
	for _, a := range result.Anomalies {
		if a.Timestamp >= burstStart && a.Timestamp < burstWindowEnd {
			fires = append(fires, a)
		}
	}
	require.NotEmpty(t, fires, "transient spike must produce at least one discord while the spike is in the scoring window")
	assert.Equal(t, "hot_sax", fires[0].DetectorName)
	assert.Contains(t, fires[0].Title, "Hot-SAX discord:")
	require.NotNil(t, fires[0].Score, "anomaly must carry a score")
	assert.Greater(t, *fires[0].Score, hotSaxScoreThreshold)
}

// TestHotSAX_FiresOnPeriodChange verifies that a change in the underlying
// periodicity is detected. After many cycles of period-8 sin, the dict is
// full of "short period" SAX words; switching to a period-24 sin produces
// words with a wholly different shape signature and high MINDIST against
// every dict entry.
//
// DEVIATION FROM PLAN: sizing — see other deviation notes. The plan asked
// for 96 warmup + 50 post-change. We extend to 240 + 240 to ensure the dict
// fills before the period change AND we have enough post-change scoring
// ticks for the new shape to register.
func TestHotSAX_FiresOnPeriodChange(t *testing.T) {
	d := testHotSAXDetector()
	storage := newTimeSeriesStorage()

	const (
		preN  = 240
		postN = 240
	)

	ts := addHotSAXPoints(storage, preN, 1, func(i int) float64 {
		return math.Sin(2 * math.Pi * float64(i) / 8.0)
	})
	changeTS := ts
	ts = addHotSAXPoints(storage, postN, ts, func(i int) float64 {
		return math.Sin(2 * math.Pi * float64(i) / 24.0)
	})

	result := d.Detect(storage, ts-1)

	// Look for at least one fire after the period change.
	postChangeFires := 0
	for _, a := range result.Anomalies {
		if a.Timestamp >= changeTS {
			postChangeFires++
		}
	}
	require.Positive(t, postChangeFires, "period change must produce at least one post-change anomaly")
}

// TestHotSAX_NoFireOnStableSeries: a long stationary series with mild
// Gaussian noise must not produce any anomalies. The MINDIST gate is what
// keeps Hot-SAX silent on stable noise — every stable window's SAX word has
// at least one near-symmetric neighbor in the dict, so MINDIST stays below
// the threshold.
func TestHotSAX_NoFireOnStableSeries(t *testing.T) {
	d := testHotSAXDetector()
	storage := newTimeSeriesStorage()

	rng := rand.New(rand.NewSource(3)) //nolint:gosec // deterministic test seed
	addHotSAXPoints(storage, 400, 1, func(_ int) float64 {
		return 50 + rng.NormFloat64()*0.5
	})

	result := d.Detect(storage, 400)
	assert.Empty(t, result.Anomalies, "stable noisy series must not fire")
}

// TestHotSAX_RemoveSeriesPrunesState: confirm the SeriesRemover hook actually
// frees the per-(ref, agg) entries. Without this hook the per-series map
// would grow unbounded as storage evicts series — that is the bug class the
// test guards. Mirrors TestLODA_RemoveSeriesClearsState /
// TestESN_RemoveSeries.
func TestHotSAX_RemoveSeriesPrunesState(t *testing.T) {
	d := NewHotSAXDetector() // default Aggregations: [Average, Count]
	storage := newTimeSeriesStorage()

	const numSeries = 5
	for s := 0; s < numSeries; s++ {
		name := "metric" + string(rune('A'+s))
		for i := 0; i < 5; i++ {
			storage.Add("ns", name, float64(i), int64(i+1), nil)
		}
	}

	d.Detect(storage, 5)
	require.Len(t, d.series, numSeries*len(d.Aggregations),
		"each series should have one state entry per aggregation")

	metas := storage.ListSeries(observer.WorkloadSeriesFilter())
	require.Len(t, metas, numSeries)

	refsToRemove := []observer.SeriesRef{metas[0].Ref, metas[1].Ref, metas[2].Ref}
	d.RemoveSeries(refsToRemove)
	assert.Len(t, d.series, (numSeries-3)*len(d.Aggregations),
		"only state for the surviving series should remain")
}

// TestHotSAX_DeterministicAcrossRuns: two fresh detectors fed identical input
// must produce byte-identical anomaly streams. This pins the deterministic
// breakpoint table, deterministic dict eviction, and the ordering of state
// updates inside processPoint.
func TestHotSAX_DeterministicAcrossRuns(t *testing.T) {
	build := func() (*HotSAXDetector, *timeSeriesStorage) {
		d := testHotSAXDetector()
		storage := newTimeSeriesStorage()
		rng := rand.New(rand.NewSource(7)) //nolint:gosec // deterministic test seed

		// Long enough to exercise warmup, dict fill, several scoring ticks,
		// and a clear shape change so we get a non-trivial anomaly stream.
		ts := addHotSAXPoints(storage, 200, 1, func(_ int) float64 {
			return 50 + rng.NormFloat64()*0.7
		})
		ts = addHotSAXPoints(storage, 5, ts, func(_ int) float64 {
			return 100 + rng.NormFloat64()*0.5
		})
		ts = addHotSAXPoints(storage, 100, ts, func(_ int) float64 {
			return 50 + rng.NormFloat64()*0.7
		})
		_ = ts
		return d, storage
	}

	d1, s1 := build()
	r1 := d1.Detect(s1, 1000)

	d2, s2 := build()
	r2 := d2.Detect(s2, 1000)

	require.Equal(t, len(r1.Anomalies), len(r2.Anomalies),
		"deterministic input must produce the same anomaly count")
	for i := range r1.Anomalies {
		a, b := r1.Anomalies[i], r2.Anomalies[i]
		assert.Equal(t, a.Timestamp, b.Timestamp, "anomaly[%d] timestamps differ", i)
		require.NotNil(t, a.Score)
		require.NotNil(t, b.Score)
		// Bit-identical scores: any drift here means a non-deterministic
		// dependency snuck into the algorithm (map iteration order, time,
		// etc.).
		assert.Equal(t, math.Float64bits(*a.Score), math.Float64bits(*b.Score),
			"anomaly[%d] scores differ bit-for-bit", i)
	}
}

// TestHotSAX_PAAToSAXEncoding pins the symbolic encoder against fixed-input
// expected outputs. This is a unit test of the encoder itself: changes to
// the breakpoint table, PAA aggregation, or symbol assignment will trip it.
//
//   - Constant input: all values are equal, stddev = 0; the encoder is
//     guarded against this and never builds a word from a flat ring (the
//     processPoint path returns early before encodeWord). We verify the
//     guard by feeding a flat series and confirming no anomaly is produced.
//   - Monotone-ascending ramp: after z-normalization, the early values are
//     deeply negative and the late values deeply positive, so the SAX word
//     must be monotone-non-decreasing across positions — i.e. word[i] <=
//     word[i+1] for all i.
func TestHotSAX_PAAToSAXEncoding(t *testing.T) {
	t.Run("flat_input_skips_dict_insert", func(t *testing.T) {
		d := testHotSAXDetector()
		storage := newTimeSeriesStorage()
		// 400 identical points: stddev is exactly 0, so the flat-line guard
		// kicks in on every scoring tick, preventing both fires AND dict
		// inserts.
		addHotSAXPoints(storage, 400, 1, func(_ int) float64 {
			return 42
		})
		result := d.Detect(storage, 400)
		assert.Empty(t, result.Anomalies, "flat input must not fire")

		// Spot-check internal state: the dict must remain empty because the
		// flat-line guard returns BEFORE the dict insert in processPoint.
		// This pins the "skip dict on flat" behavior.
		for sk, st := range d.series {
			assert.Equalf(t, 0, st.dictFill, "dict must remain empty on flat input (state %v)", sk)
		}
	})

	t.Run("monotone_ramp_yields_monotone_word", func(t *testing.T) {
		// Direct unit test of encodeWord on a synthetic ring. We construct a
		// monotone ramp 0..31 placed in the ring with ringHead=0 (so logical
		// order matches storage order), and verify the resulting SAX word
		// is monotone-non-decreasing.
		d := NewHotSAXDetector()
		var ring [hotSaxWindowSize]float64
		for i := 0; i < hotSaxWindowSize; i++ {
			ring[i] = float64(i)
		}
		mean, stddev := ringMeanStd(&ring, hotSaxWindowSize)
		require.Greater(t, stddev, 0.0)

		var word [hotSaxWordLen]uint8
		d.encodeWord(&ring, 0, mean, stddev, &word)

		for i := 1; i < hotSaxWordLen; i++ {
			assert.GreaterOrEqualf(t, word[i], word[i-1],
				"monotone-ascending input must produce a monotone-non-decreasing SAX word; got %v", word)
		}
		// The endpoints must be at the alphabet extremes — a 0..31 ramp is
		// far outside ±0.6745σ at both tails.
		assert.Equalf(t, uint8(0), word[0], "first PAA segment of ascending ramp should be the lowest symbol; got word=%v", word)
		assert.Equalf(t, uint8(hotSaxAlphabetSize-1), word[hotSaxWordLen-1],
			"last PAA segment of ascending ramp should be the highest symbol; got word=%v", word)
	})
}
