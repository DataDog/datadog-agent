// testHotSAXDetector returns a Hot-SAX detector pinned to AverageAggregate so
// per-test bookkeeping (state-key counts, anomaly counts) stays predictable —
// matches the convention in metrics_detector_loda_test.go / esn_test.go.
func testHotSAXDetector() *HotSAXDetector {
	d := NewHotSAXDetector()
	d.Aggregations = []observer.Aggregate{observer.AggregateAverage}
	return d
}

// addHotSAXPoints mirrors addESNPoints / addLODAPoints — start at timestamp
// `startTS`, push `n` points generated by valueFn(i) at unit-second intervals.
// Returns the next-available timestamp so tests can chain phases.
func addHotSAXPoints(storage *timeSeriesStorage, n int, startTS int64, valueFn func(i int) float64) int64 {
	ts := startTS
	for i := 0; i < n; i++ {
		storage.Add("ns", "metric", valueFn(i), ts, nil)
		ts++
	}
	return ts
}

