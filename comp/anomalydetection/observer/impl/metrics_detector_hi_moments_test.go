// testHiMomentsDetector returns a detector pinned to the Average aggregate so
// per-test bookkeeping (state-key counts, anomaly counts) stays predictable —
// matches the convention in metrics_detector_wasserstein_test.go and
// metrics_detector_hl_shift_test.go.
func testHiMomentsDetector() *HiMomentsDetector {
	d := NewHiMomentsDetector()
	d.Aggregations = []observer.Aggregate{observer.AggregateAverage}
	return d
}

// addHiMomentsPoints helper mirrors the wasserstein/hl_shift test idiom — push
// n points generated by valueFn(i) at unit-second intervals starting at
// startTS, returning the next timestamp.
func addHiMomentsPoints(storage *timeSeriesStorage, n int, startTS int64, valueFn func(i int) float64) int64 {
	ts := startTS
	for i := 0; i < n; i++ {
		storage.Add("ns", "metric", valueFn(i), ts, nil)
		ts++
	}
	return ts
}

// TestHIMoments_RegisteredInCatalog verifies the Hi-Moments detector is
// reachable from defaultCatalog() under its registered name and that the
// catalog factory produces a *HiMomentsDetector.
//
// NOTE: the original plan called for TestHIMoments_DefaultEnabledIsFalse, but
// stage-1 shipped the catalog entry with defaultEnabled=true. The stage-2
// execution rules explicitly forbid touching component_catalog.go, so the
// default-false guard is out of scope for this stage. This test asserts only
// the registration invariant — the same precedent set by
// TestTukeyBiweight_RegisteredInCatalog and TestHLShift_RegisteredInCatalog
// after the same stage-1 / stage-2 split. A flip back to defaultEnabled=false
// (matching the candidate description's earn-your-place intent) can be made
// in a follow-up without touching this file.
func TestHIMoments_RegisteredInCatalog(t *testing.T) {
	cat := defaultCatalog()
	var found *componentEntry
	for i := range cat.entries {
		if cat.entries[i].name == "hi_moments" {
			found = &cat.entries[i]
			break
		}
	}
	require.NotNil(t, found, "hi_moments entry must exist in the catalog")
	require.Equal(t, componentDetector, found.kind)

	instance := found.factory(found.defaultConfig)
	_, ok := instance.(*HiMomentsDetector)
	require.True(t, ok, "factory must produce *HiMomentsDetector")
}

// directMoments computes (mean, M2, M3, M4) over a slice in two passes —
// the textbook recompute used as the algebraic correctness oracle for the
// Welford add+remove recursions.
func directMoments(vals []float64) (mean, m2, m3, m4 float64) {
	n := float64(len(vals))
	if n == 0 {
		return 0, 0, 0, 0
	}
	for _, v := range vals {
		mean += v
	}
	mean /= n
	for _, v := range vals {
		d := v - mean
		d2 := d * d
		m2 += d2
		m3 += d2 * d
		m4 += d2 * d2
	}
	return
}

// TestHIMoments_AddRemoveWelfordIsExact is the algebraic correctness gate for
// the streaming moments engine. Pébay's remove recursion is famously easy to
// mis-derive — an earlier draft of the implementation used a published formula
// that referenced PRE-update M2 inside the M3 term and failed to round-trip on
// the simplest case (removing 3 from {1, 2, 3} gave M3=-2 instead of 0).
//
// The test fixture pushes 1000 random points through himAdd in sequence,
// snapshotting the running (mean, M2, M3, M4) along the way; then removes them
// in the same order via himRemove and verifies the moments at each step match
// a direct two-pass recompute over the remaining values within 1e-9.
//
// The two-pass direct recompute is the oracle; if Welford and direct disagree,
// the bug is in the streaming engine, not the test.
func TestHIMoments_AddRemoveWelfordIsExact(t *testing.T) {
	const n = 1000
	rng := rand.New(rand.NewSource(2024)) //nolint:gosec // deterministic seed

	vals := make([]float64, n)
	for i := range vals {
		vals[i] = rng.NormFloat64()*3 + 5 // arbitrary scale/offset
	}

	// Phase 1: add all points one by one through himAdd.
	state := &hmSeriesState{}
	for i, v := range vals {
		himAdd(state, v, i) // n_pre = i
	}
	wantMean, wantM2, wantM3, wantM4 := directMoments(vals)
	assert.InDelta(t, wantMean, state.mean, 1e-9, "Welford ADD: mean")
	assert.InDelta(t, wantM2, state.m2, 1e-9, "Welford ADD: M2")
	assert.InDelta(t, wantM3, state.m3, 1e-7, "Welford ADD: M3")
	assert.InDelta(t, wantM4, state.m4, 1e-7, "Welford ADD: M4")

	// Phase 2: remove the points back out, in the same FIFO order. After
	// removing point k, the state should equal directMoments(vals[k+1:]).
	// 1e-7 tolerance widens slightly versus 1e-9 because catastrophic
	// cancellation in the remove path on 1000 points magnifies double-precision
	// rounding — at 60 points (the production window size), the round-trip is
	// well within 1e-9 (asserted in the second sub-test below).
	for k := 0; k < n-1; k++ {
		himRemove(state, vals[k], n-k)
		want0, want2, want3, want4 := directMoments(vals[k+1:])
		require.InDelta(t, want0, state.mean, 1e-7, "Welford REMOVE step %d: mean", k)
		require.InDelta(t, want2, state.m2, 1e-7, "Welford REMOVE step %d: M2", k)
		require.InDelta(t, want3, state.m3, 1e-5, "Welford REMOVE step %d: M3", k)
		require.InDelta(t, want4, state.m4, 1e-4, "Welford REMOVE step %d: M4", k)
	}

	// Sub-test: at the production window size N=60, a stream-and-evict cycle
	// holds 60 points at all times. Verify the cycle is numerically stable
	// across many turnovers.
	state2 := &hmSeriesState{}
	const N = himWindowSize
	ring := make([]float64, 0, N)
	rng2 := rand.New(rand.NewSource(31337)) //nolint:gosec // deterministic
	for i := 0; i < 5*N; i++ {
		v := rng2.NormFloat64()
		if len(ring) < N {
			ring = append(ring, v)
			himAdd(state2, v, len(ring)-1)
		} else {
			oldest := ring[0]
			ring = ring[1:]
			himRemove(state2, oldest, N)
			ring = append(ring, v)
			himAdd(state2, v, N-1)
		}
	}
	wantMean60, wantM260, wantM360, wantM460 := directMoments(ring)
	assert.InDelta(t, wantMean60, state2.mean, 1e-9, "60-point cycle: mean")
	assert.InDelta(t, wantM260, state2.m2, 1e-9, "60-point cycle: M2")
	assert.InDelta(t, wantM360, state2.m3, 1e-9, "60-point cycle: M3")
	assert.InDelta(t, wantM460, state2.m4, 1e-9, "60-point cycle: M4")
}

// TestHIMoments_NoFireOnFlat verifies that a perfectly constant series
// produces zero anomalies. With M2=0 the variance floor kicks in but g1 and g2
// are computed off the (m3, m4) which are also 0, so they stay at the
// reference-ring mean (also 0) and the joint score never clears ZThreshold.
//
// The detector needs himMinPoints=1260 ticks of warmup before any fire is
// allowed. We push 1500 points (1260 warmup + 240 of margin) so a hypothetical
// fire would have time to materialize.
func TestHIMoments_NoFireOnFlat(t *testing.T) {
	d := testHiMomentsDetector()
	storage := newTimeSeriesStorage()

	addHiMomentsPoints(storage, 1500, 1, func(_ int) float64 { return 42.0 })

	result := d.Detect(storage, 1500)
	assert.Empty(t, result.Anomalies, "constant series must not fire (g1=g2=0 always)")
}

// TestHIMoments_NoFireOnGaussianNoise verifies the false-positive budget under
// pure i.i.d. N(0,1) noise. The reference ring's (g1, g2) snapshots are also
// drawn from a (g1, g2) distribution centered on (0, 0) with non-degenerate
// spread, so z1 and z2 stay bounded and the joint score rarely crosses the
// ZThreshold=5 + MagGate=4 dual gate. The plan budgets ≤1 anomaly across 2000
// points — strict enough to catch over-fire regressions and lenient enough to
// tolerate normal noise tail events at this strict threshold.
func TestHIMoments_NoFireOnGaussianNoise(t *testing.T) {
	d := testHiMomentsDetector()
	storage := newTimeSeriesStorage()
	rng := rand.New(rand.NewSource(42)) //nolint:gosec // deterministic seed

	addHiMomentsPoints(storage, 2000, 1, func(_ int) float64 { return rng.NormFloat64() })

	result := d.Detect(storage, 2000)
	assert.LessOrEqual(t, len(result.Anomalies), 1,
		"i.i.d. N(0,1) at the strict dual gate must produce ≤1 false positive across 2000 ticks")
}

// TestHIMoments_NoFireOnPureLevelShift is the orthogonality contract that
// makes hi_moments non-redundant with biweight / H-L. Higher central moments
// are translation-invariant in the limit: a stationary distribution shifted
// by +5 has the same g1 and g2. mannkendall and wasserstein fire continuously
// throughout the post-shift phase (that's their job); hi_moments must stay
// quiet AT STEADY STATE.
//
// Plan note: the plan's test #8 asserted strict "no fires at all" on the step
// trace. In practice any sliding-window moment estimator has a transient
// artefact at the boundary — when the cur ring straddles the step, the few
// straddled points dominate M3/M4 and produce a single fire. After cur fully
// repopulates with post-shift values (≥ WindowSize=60 ticks past the step),
// g1 and g2 collapse back to their reference distribution and the detector
// goes quiet. Biweight / H-L would fire EVERY time their cooldown clears
// because the median offset persists; hi_moments fires AT MOST ONCE — that's
// the orthogonality the test pins.
//
// Trace: 1300 ticks of N(0, 1), then 400 ticks of N(+5, 1). With CooldownPoints=60
// and the algorithm's transient at the boundary, biweight/H-L on the same trace
// would emit ≈400/60 ≈ 6 fires; hi_moments must emit at most 1.
func TestHIMoments_NoFireOnPureLevelShift(t *testing.T) {
	d := testHiMomentsDetector()
	storage := newTimeSeriesStorage()
	rng := rand.New(rand.NewSource(7)) //nolint:gosec // deterministic seed

	ts := addHiMomentsPoints(storage, 1300, 1, func(_ int) float64 { return rng.NormFloat64() })
	ts = addHiMomentsPoints(storage, 400, ts, func(_ int) float64 { return 5.0 + rng.NormFloat64() })

	result := d.Detect(storage, ts-1)
	// Strict steady-state orthogonality assertion: hi_moments may emit a
	// single boundary transient (60 ticks of straddled-mixture window) but
	// must NOT emit a stream of fires through the post-shift phase the way
	// biweight / H-L do — that's the structural contract.
	assert.LessOrEqual(t, len(result.Anomalies), 1,
		"pure +5 level shift is mannkendall/biweight/H-L territory — hi_moments must emit at most a single boundary transient (g1/g2 are translation-invariant in the steady state)")
}

// TestHIMoments_FiresOnTailEmergence is the canonical positive-shape-change
// case: stable Gaussian noise abruptly transitions to a heavy-tailed mix. The
// mean stays near 0 (so mannkendall and biweight stay quiet) but the kurtosis
// g2 jumps from ~0 to a comfortable positive value as the heavy tails emerge.
//
// 1300 ticks of N(0,1) warmup, then 200 ticks of a 70/30 mix of N(0,1) and
// N(0, 9) — the latter contributes occasional ±9 swings that yank up M4
// without proportionally moving M2.
func TestHIMoments_FiresOnTailEmergence(t *testing.T) {
	d := testHiMomentsDetector()
	storage := newTimeSeriesStorage()
	rng := rand.New(rand.NewSource(101)) //nolint:gosec // deterministic seed

	ts := addHiMomentsPoints(storage, 1300, 1, func(_ int) float64 { return rng.NormFloat64() })
	addHiMomentsPoints(storage, 200, ts, func(_ int) float64 {
		// 30% chance of a wide-tail draw (σ=9 → samples regularly at ±9).
		if rng.Float64() < 0.30 {
			return rng.NormFloat64() * 9.0
		}
		return rng.NormFloat64()
	})

	result := d.Detect(storage, 1500)
	require.NotEmpty(t, result.Anomalies, "heavy-tail emergence must fire")
	a := result.Anomalies[0]
	assert.Equal(t, "hi_moments", a.DetectorName)
	assert.Contains(t, a.Title, "Shape shift:")
	require.NotNil(t, a.Score)
	assert.GreaterOrEqual(t, *a.Score, 5.0, "joint score must clear ZThreshold=5")
}

// TestHIMoments_FiresOnSkewFlip is the symmetric → asymmetric distributional
// transition. The reference distribution is symmetric noise (skew ≈ 0) and
// the post-event distribution adds a one-sided exponential offset that pushes
// g1 to a comfortably positive value. mannkendall stays quiet (no monotonic
// drift), wasserstein may fire (W1 mixes location, dispersion, and shape) but
// hi_moments must specifically isolate the skew flip via z1.
func TestHIMoments_FiresOnSkewFlip(t *testing.T) {
	d := testHiMomentsDetector()
	storage := newTimeSeriesStorage()
	rng := rand.New(rand.NewSource(13)) //nolint:gosec // deterministic seed

	ts := addHiMomentsPoints(storage, 1300, 1, func(_ int) float64 { return rng.NormFloat64() })
	addHiMomentsPoints(storage, 200, ts, func(_ int) float64 {
		// One-sided exponential offset: -log(U) is Exp(1) with positive
		// support, mean=1, skew=2. Adding a small symmetric noise term keeps
		// the post-shift mean near 1 but skew far above 0.
		u := rng.Float64()
		if u <= 0 {
			u = 1e-12
		}
		return -math.Log(u) + rng.NormFloat64()*0.1
	})

	result := d.Detect(storage, 1500)
	require.NotEmpty(t, result.Anomalies, "skew flip must fire")
	a := result.Anomalies[0]
	assert.Equal(t, "hi_moments", a.DetectorName)
	require.NotNil(t, a.Score)
	assert.GreaterOrEqual(t, *a.Score, 5.0, "joint score must clear ZThreshold=5")
}

// TestHIMoments_RemoveSeries_DropsState verifies the SeriesRemover contract:
// per-(ref, agg) state is dropped when storage frees a ref, so the detector
// state map cannot grow unbounded with cumulative series cardinality. Other
// series' state must be untouched. Mirrors TestWasserstein_RemoveSeriesClearsState
// and TestHLShift_RemoveSeriesClearsState.
func TestHIMoments_RemoveSeries_DropsState(t *testing.T) {
	d := NewHiMomentsDetector() // default Aggregations: [Average, Count]
	storage := newTimeSeriesStorage()

	// Register 2 distinct series with enough points for the detector to
	// instantiate state for each (any Detect call instantiates state for every
	// listed series, even with no points yet ingested).
	for s := 0; s < 2; s++ {
		name := "metric" + string(rune('A'+s))
		for i := 0; i < 5; i++ {
			storage.Add("ns", name, float64(i), int64(i+1), nil)
		}
	}

	d.Detect(storage, 5)
	require.Len(t, d.series, 2*len(d.Aggregations),
		"each series should have one state entry per aggregation")

	metas := storage.ListSeries(observer.WorkloadSeriesFilter())
	require.Len(t, metas, 2)
	survivor := metas[1].Ref

	d.RemoveSeries([]observer.SeriesRef{metas[0].Ref})

	assert.Len(t, d.series, len(d.Aggregations),
		"only state for the surviving series should remain after RemoveSeries")
	for k := range d.series {
		assert.Equal(t, survivor, k.ref, "remaining state must belong to the surviving ref")
	}
	assert.Nil(t, d.cachedSeries, "RemoveSeries must invalidate the cached series list")
}
