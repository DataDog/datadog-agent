// TestGARCH_ImplementsSeriesRemover guards the catalog teardown contract: the
// detector keeps a per-(ref, agg) state map and therefore MUST implement
// observer.SeriesRemover so the engine can free state when storage evicts
// series. Without this hook the per-series map would grow unbounded.
//
// Same shape as TestESN_RemoveSeries: insert N series, run Detect to populate
// state, then call RemoveSeries on a subset and verify the map shrinks by
// exactly the expected count.
func TestGARCH_ImplementsSeriesRemover(t *testing.T) {
	// Compile-time guarantee that the detector type satisfies the interface.
	var _ observer.SeriesRemover = (*GarchVolatilityDetector)(nil)

	d := NewGarchVolatilityDetector() // default Aggregations: [Average, Count]
	storage := newTimeSeriesStorage()

	const numSeries = 5
	for s := 0; s < numSeries; s++ {
		name := "metric" + string(rune('A'+s))
		for i := 0; i < 5; i++ {
			storage.Add("ns", name, float64(i), int64(i+1), nil)
		}
	}

	d.Detect(storage, 5)
	require.Len(t, d.series, numSeries*len(d.Aggregations),
		"each series should have one state entry per aggregation")

	metas := storage.ListSeries(observer.WorkloadSeriesFilter())
	require.Len(t, metas, numSeries)

	refsToRemove := []observer.SeriesRef{metas[0].Ref, metas[1].Ref, metas[2].Ref}
	d.RemoveSeries(refsToRemove)
	assert.Len(t, d.series, (numSeries-3)*len(d.Aggregations),
		"only state for the surviving series should remain")
}

// TestGARCH_NoNaNUnderConstantInput verifies numerical hygiene: feeding 256
// identical values must produce no fires and must not propagate NaN through
// the σ² recurrence or the score path.
//
// Behavior: with constant y, all innovations after the first-tick init are 0,
// so σ² evolves as σ²_{t+1} = ω + β·σ²_t (the α-term vanishes). Equilibrium
// is ω/(1-β) ≈ 6.67e-6 — small but strictly positive. The 1e-12 floor in
// processPoint protects sqrt against an even smaller σ² if the recurrence
// undershoots. innov is 0, so |z| is 0 and no fire is possible.
//
// (NOTE: the plan referenced ω/(1-α-β) as the equilibrium for this test; that
// is the equilibrium under the GARCH-process assumption r² ≡ σ², which does
// not hold for constant input. The correct asymptote here is ω/(1-β).)
func TestGARCH_NoNaNUnderConstantInput(t *testing.T) {
	d := testGARCHDetector()
	storage := newTimeSeriesStorage()

	const n = 256
	const constVal = 42.0
	ts := addGARCHPoints(storage, n, 1, func(_ int) float64 {
		return constVal
	})

	result := d.Detect(storage, ts-1)
	assert.Empty(t, result.Anomalies, "constant input must not fire")

	// Inspect the single state entry directly: σ² must be finite and >= 0.
	require.Len(t, d.series, 1, "exactly one (ref, agg) state should exist")
	for _, st := range d.series {
		assert.False(t, math.IsNaN(st.sigma2), "σ² must not be NaN")
		assert.False(t, math.IsInf(st.sigma2, 0), "σ² must not be Inf")
		assert.GreaterOrEqual(t, st.sigma2, 0.0, "σ² must be non-negative")
		assert.False(t, math.IsNaN(st.mu), "μ must not be NaN")
		assert.Equal(t, constVal, st.mu, "μ on constant input should remain at the value")
	}
}
