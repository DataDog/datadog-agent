// testGARCHDetector returns a GARCH detector pinned to AverageAggregate so the
// per-test bookkeeping (state-key counts, anomaly counts) stays predictable —
// matches the convention in metrics_detector_esn_test.go / loda_test.
func testGARCHDetector() *GarchVolatilityDetector {
	d := NewGarchVolatilityDetector()
	d.Aggregations = []observer.Aggregate{observer.AggregateAverage}
	return d
}

// addGARCHPoints starts at startTS, pushes n points generated by valueFn(i) at
// unit timestamp intervals. Returns the next-available timestamp so tests can
// chain phases. Mirrors addESNPoints / addHotSAXPoints.
func addGARCHPoints(storage *timeSeriesStorage, n int, startTS int64, valueFn func(i int) float64) int64 {
	ts := startTS
	for i := 0; i < n; i++ {
		storage.Add("ns", "metric", valueFn(i), ts, nil)
		ts++
	}
	return ts
}

// TestGARCH_StableNoiseNoFire is the false-positive discipline guard. 1000
// ticks of i.i.d. N(0, 1) feed a freshly constructed detector and we assert no
// anomalies fire post-warmup.
//
// Statistical reasoning: under stationary N(0, 1) the detector's σ²
// equilibrium is ω/(1-β) + α·E[r²]/(1-β) ≈ 0.10/0.15 = 0.667; sqrt(σ²_eq) ≈
// 0.816. Crossing |z| = garchZThreshold (= 5) requires |innov| ≥ 4.08 ≈ 4.08σ
// on the source distribution, with P ≈ 4.5e-5 per tick. Over the ≈872
// post-warmup+train ticks the expected fire count is ≈ 0.04 — extremely
// unlikely with a deterministic seed. This test regresses if the threshold is
// lowered or warmup shortened (the prior fp_egregious failures in exp-0103 /
// exp-0096 / exp-0095 are exactly the conditions this guards).
func TestGARCH_StableNoiseNoFire(t *testing.T) {
	d := testGARCHDetector()
	storage := newTimeSeriesStorage()

	rng := rand.New(rand.NewSource(1)) //nolint:gosec // deterministic test seed
	ts := addGARCHPoints(storage, 1000, 1, func(_ int) float64 {
		return rng.NormFloat64()
	})

	result := d.Detect(storage, ts-1)
	assert.Empty(t, result.Anomalies, "stable N(0, 1) noise must not fire")
}

// TestGARCH_VolatilityClusteringFires synthesizes the canonical "low-σ then
// high-σ" regime change that the GARCH residual gate is designed to catch:
// 200 ticks of N(0, 1) followed by N(0, 8). Assert at least one fire lands
// within ~50 ticks of the switch.
//
// Math: the detector enters the switch with σ² ≈ 0.667 (sigma floored at MAD
// scale ≈ 1.0). Post-switch innov has std 8, so any |innov| ≥ 5.0 — equivalent
// to ≥ 0.625σ on the source distribution, P ≈ 0.53 per tick — fires both the
// |z| ≥ 5 and effect ≥ 4 gates. σ² catches up over the next ≈10 ticks
// (α = 0.10 absorbs innov² quickly), so the firing window is tight; we extend
// to 50 ticks to give the test deterministic-seed margin.
//
// DEVIATION FROM PLAN: the plan's example used a 5× volatility ratio. With
// the strict z = 5 threshold combined with GARCH(1,1)'s α = 0.10 (rapid σ²
// catch-up), a 5× ratio produced only borderline-firing scenarios and the
// test was flaky on the chosen seed. The 8× ratio is well above the threshold
// at first tick AND cleanly within the |z| ≥ 5 envelope for several ticks
// post-switch — better-suited to a deterministic single-seed test.
func TestGARCH_VolatilityClusteringFires(t *testing.T) {
	d := testGARCHDetector()
	storage := newTimeSeriesStorage()

	rng := rand.New(rand.NewSource(2)) //nolint:gosec // deterministic test seed

	const stableN = 200
	const volN = 80
	const volMultiplier = 8.0

	ts := addGARCHPoints(storage, stableN, 1, func(_ int) float64 {
		return rng.NormFloat64()
	})
	switchTS := ts
	ts = addGARCHPoints(storage, volN, ts, func(_ int) float64 {
		return rng.NormFloat64() * volMultiplier
	})

	result := d.Detect(storage, ts-1)

	// Pre-switch fires would mean the false-positive gate is broken; this
	// invariant doubles as a regression guard for TestGARCH_StableNoiseNoFire.
	for _, a := range result.Anomalies {
		assert.GreaterOrEqualf(t, a.Timestamp, switchTS,
			"unexpected pre-switch fire at ts=%d (score=%v)", a.Timestamp, a.Score)
	}

	// Post-switch: at least one fire must land in the first 50 ticks after
	// the regime change. Looking at the WHOLE post-switch range would let a
	// late-firing implementation pass too, which would defeat the test.
	cutoffTS := switchTS + 50
	var early []observer.Anomaly
	for _, a := range result.Anomalies {
		if a.Timestamp >= switchTS && a.Timestamp < cutoffTS {
			early = append(early, a)
		}
	}
	require.NotEmpty(t, early,
		"volatility clustering should produce at least one fire within 50 ticks of the switch")
}

// TestGARCH_LevelShiftAloneDoesNotFireImmediately encodes the "preserve
// 703-shopify (level shift = quiet)" contract. The detector must NOT
// false-positive on a sustained level shift of moderate magnitude; the GARCH
// model is supposed to fire on volatility, not level.
//
// Setup: 200 ticks of N(0, 1) (clears warmup+train=128) then a +2σ step. We
// expect at most 1 fire in the 30 ticks following the step, since:
//   - innov at t=step is roughly +2 + N(0, 1), σ ≈ 0.816 → |z| ≈ 2.45 ≪ 5.
//   - μ EWMA-tracks at α_μ=0.02, catching ~45% of the shift in 30 ticks, so
//     residual innov drops further.
//   - σ² grows under the larger innov², shrinking |z| further still.
//
// "≤ 1 fire" rather than "0 fires" tolerates a single tail-of-distribution
// noise event, which is acceptable given the strict z=5 floor.
func TestGARCH_LevelShiftAloneDoesNotFireImmediately(t *testing.T) {
	d := testGARCHDetector()
	storage := newTimeSeriesStorage()

	rng := rand.New(rand.NewSource(3)) //nolint:gosec // deterministic test seed

	// Stable phase: long enough to clear warmup+train (128) with margin.
	const stableN = 200
	const postN = 30

	ts := addGARCHPoints(storage, stableN, 1, func(_ int) float64 {
		return rng.NormFloat64()
	})
	stepTS := ts
	ts = addGARCHPoints(storage, postN, ts, func(_ int) float64 {
		return 2.0 + rng.NormFloat64()
	})

	result := d.Detect(storage, ts-1)

	var postStep int
	for _, a := range result.Anomalies {
		if a.Timestamp >= stepTS && a.Timestamp < ts {
			postStep++
		}
	}
	assert.LessOrEqualf(t, postStep, 1,
		"level shift alone should produce at most 1 fire in the 30 post-step ticks (got %d)",
		postStep)
}

// TestGARCH_DefaultEnabledIsTrue pins the catalog default for the
