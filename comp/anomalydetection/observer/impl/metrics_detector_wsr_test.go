// testWSRDetector returns a detector pinned to the Average aggregate so
// per-test bookkeeping (state-key counts, anomaly counts) stays predictable —
// matches the convention in metrics_detector_hl_shift_test.go.
func testWSRDetector() *WSRDetector {
	d := NewWSRDetector()
	d.Aggregations = []observer.Aggregate{observer.AggregateAverage}
	return d
}

// addWSRPoints pushes n points generated by valueFn(i) at unit-second
// intervals starting from startTS, returning the next timestamp.
func addWSRPoints(storage *timeSeriesStorage, n int, startTS int64, valueFn func(i int) float64) int64 {
	ts := startTS
	for i := 0; i < n; i++ {
		storage.Add("ns", "metric", valueFn(i), ts, nil)
		ts++
	}
	return ts
}

// TestWSR_Name pins the catalog-registered detector name. Renaming this
// without updating component_catalog.go would silently break
// q.eval-scenarios --only wsr.
func TestWSR_Name(t *testing.T) {
	d := NewWSRDetector()
	assert.Equal(t, "wsr", d.Name())
}

// TestWSR_RegisteredInCatalog verifies the WSR detector is reachable from
// defaultCatalog() under its registered name and that the catalog factory
// produces a *WSRDetector with defaultEnabled=true. Mirrors
// TestHLShift_RegisteredInCatalog — the assertion shape that catalog tests
// across this package follow.
func TestWSR_RegisteredInCatalog(t *testing.T) {
	cat := defaultCatalog()
	var found *componentEntry
	for i := range cat.entries {
		if cat.entries[i].name == "wsr" {
			found = &cat.entries[i]
			break
		}
	}
	require.NotNil(t, found, "wsr entry must exist in the catalog")
	require.Equal(t, componentDetector, found.kind)
	require.False(t, found.defaultEnabled, "manual corpus candidates should be --only addressable, not default-enabled")

	instance := found.factory(found.defaultConfig)
	_, ok := instance.(*WSRDetector)
	require.True(t, ok, "factory must produce *WSRDetector")
}

// TestWSR_DetectsLevelShift exercises the canonical positive case: 50 ref
// points ~ N(0, 1) followed by 50 cur points ~ N(3, 1). With ScoreEvery=1 the
// detector scores every tick once both windows are full, so it gets several
// chances to fire as the shifted block displaces the original noise in cur.
// A clean +3σ shift produces |z| well past 4 and a shiftMAD comfortably above
// the 1.5 effect-size gate — exactly one fire is expected (cooldown=30
// suppresses the rest).
func TestWSR_DetectsLevelShift(t *testing.T) {
	d := testWSRDetector()
	d.ScoreEvery = 1
	storage := newTimeSeriesStorage()
	rng := rand.New(rand.NewSource(42)) //nolint:gosec // deterministic test seed

	ts := addWSRPoints(storage, 50, 1, func(_ int) float64 { return rng.NormFloat64() })
	ts = addWSRPoints(storage, 50, ts, func(_ int) float64 { return 3.0 + rng.NormFloat64() })

	result := d.Detect(storage, ts-1)
	require.NotEmpty(t, result.Anomalies, "a clean +3σ shift must fire at least once")

	a := result.Anomalies[0]
	assert.Equal(t, "wsr", a.DetectorName)
	assert.Contains(t, a.Title, "WSR shift:")
	assert.Contains(t, a.Description, "increasing")
	require.NotNil(t, a.Score, "anomaly must carry a score")
	assert.GreaterOrEqual(t, *a.Score, 4.0, "|z|-derived score must clear ZThreshold")
}

// TestWSR_NoFireOnStableNoise pins the false-positive budget under pure
// N(0, 1) noise. With ZThreshold=4 and the additional shiftMAD>=1.5
// effect-size gate, no scoring tick on i.i.d. noise should clear both gates;
// allow ≤1 anomaly to give the same noise budget as MannKendall / HL Shift.
func TestWSR_NoFireOnStableNoise(t *testing.T) {
	d := testWSRDetector()
	storage := newTimeSeriesStorage()
	rng := rand.New(rand.NewSource(42)) //nolint:gosec // deterministic test seed

	addWSRPoints(storage, 200, 1, func(_ int) float64 { return rng.NormFloat64() })

	result := d.Detect(storage, 200)
	assert.LessOrEqual(t, len(result.Anomalies), 1, "pure N(0,1) noise must fire at most once")
}

// TestWSR_TieHandling feeds a degenerate constant-ref / constant-cur input
// where every |d_i| collapses to a single tie group. The mid-rank tie
// resolution and the (t³-t)/48 variance correction both have to kick in for
// the test to produce a finite, large |z|. This pins:
//
//   - no NaN / panic from the ranking pass when every entry ties,
//   - the tie-corrected variance does not collapse to ≤ 1e-12 (which would
//     trigger the abstain branch),
//   - the shiftMAD scale-stable fallback (madRef=0 → |medRef|·0.01) lets the
//     effect-size gate pass on a constant ref.
func TestWSR_TieHandling(t *testing.T) {
	d := testWSRDetector()
	d.ScoreEvery = 1
	storage := newTimeSeriesStorage()

	// 40 ref points all at 1.0, then 40 cur points all at 5.0. Every d_i = 4.0
	// and shares the same |d|, so the entire run is one tied group.
	ts := addWSRPoints(storage, 40, 1, func(_ int) float64 { return 1.0 })
	ts = addWSRPoints(storage, 40, ts, func(_ int) float64 { return 5.0 })

	result := d.Detect(storage, ts-1)
	require.NotEmpty(t, result.Anomalies,
		"all-tied positive d_i must produce a well-defined fire (W=820, z≈6.3 at N=40)")

	a := result.Anomalies[0]
	require.NotNil(t, a.Score)
	assert.False(t, isNaNFloat(*a.Score), "score must not be NaN under all-tied input")
	assert.GreaterOrEqual(t, *a.Score, 4.0, "|z| must clear ZThreshold even with full tie correction")
	assert.Contains(t, a.Description, "increasing")
}

// TestWSR_RemoveSeriesClearsState verifies the SeriesRemover contract:
// per-(ref, agg) state is dropped when storage frees a ref, so the detector
// state map cannot grow unbounded with cumulative series cardinality. Other
// series' state must be untouched. Mirrors TestHLShift_RemoveSeriesClearsState.
func TestWSR_RemoveSeriesClearsState(t *testing.T) {
	d := NewWSRDetector() // default Aggregations: [Average, Count]
	storage := newTimeSeriesStorage()

	// Register 2 distinct series with enough points to land in the detector's
	// per-series map (any Detect call instantiates state for every listed
	// series, even if no points have been ingested yet).
	for s := 0; s < 2; s++ {
		name := "metric" + string(rune('A'+s))
		for i := 0; i < 5; i++ {
			storage.Add("ns", name, float64(i), int64(i+1), nil)
		}
	}

	d.Detect(storage, 5)
	require.Len(t, d.series, 2*len(d.Aggregations),
		"each series should have one state entry per aggregation")

	metas := storage.ListSeries(observer.WorkloadSeriesFilter())
	require.Len(t, metas, 2)
	survivor := metas[1].Ref

	d.RemoveSeries([]observer.SeriesRef{metas[0].Ref})

	// Only state for the surviving series should remain — and only for that
	// ref·agg pair.
	assert.Len(t, d.series, len(d.Aggregations),
		"only state for the surviving series should remain after RemoveSeries")
	for k := range d.series {
		assert.Equal(t, survivor, k.ref, "remaining state must belong to the surviving ref")
	}
	assert.Nil(t, d.cachedSeries, "RemoveSeries must invalidate the cached series list")
}

// isNaNFloat is a tiny helper to keep the test assertion readable without
// pulling math into the test imports just for the NaN check.
func isNaNFloat(f float64) bool { return f != f }
