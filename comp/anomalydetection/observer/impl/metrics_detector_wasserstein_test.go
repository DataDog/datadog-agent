// testWassersteinDetector returns a detector restricted to the average
// aggregate so per-test bookkeeping (state-key counts, anomaly counts) stays
// predictable — matches the convention in metrics_detector_mannkendall_test.go.
func testWassersteinDetector() *WassersteinDetector {
	d := NewWassersteinDetector()
	d.Aggregations = []observer.Aggregate{observer.AggregateAverage}
	return d
}

// addWassersteinPoints helper mirrors the loda/scanmw test idiom — push n
// points generated by valueFn(i) at unit-second intervals, returning the next
// timestamp.
func addWassersteinPoints(storage *timeSeriesStorage, n int, startTS int64, valueFn func(i int) float64) int64 {
	ts := startTS
	for i := 0; i < n; i++ {
		storage.Add("ns", "metric", valueFn(i), ts, nil)
		ts++
	}
	return ts
}

// TestWasserstein_DefaultEnabledIsFalse pins the catalog default for the
// wasserstein entry. Several prior experiments (including this detector
// family — exp-0059) were rejected for shipping defaultEnabled:true on an
// unproven detector. The plan ships defaultEnabled=false and this regression
// test catches a future drive-by flip in CI before review.
func TestWasserstein_DefaultEnabledIsFalse(t *testing.T) {
	cat := defaultCatalog()
	for _, e := range cat.entries {
		if e.name == "wasserstein" {
			assert.False(t, e.defaultEnabled,
				"wasserstein must default to disabled until it has earned a place in the prod-realistic baseline")
			return
		}
	}
	t.Fatal("wasserstein entry not present in default catalog")
}

// TestWasserstein_NoFireOnStableSeries verifies the detector ignores a stable
// periodic signal. A sinusoid of fixed amplitude/frequency produces ref and
// cur snapshots with near-identical sorted distributions, so W1 ≈ 0 and the
// distance gate stays well below DistanceThresholdMAD.
func TestWasserstein_NoFireOnStableSeries(t *testing.T) {
	d := testWassersteinDetector()
	storage := newTimeSeriesStorage()

	addWassersteinPoints(storage, 200, 1, func(i int) float64 {
		return math.Sin(2*math.Pi*float64(i)/30.0)*0.5 + 10.0
	})

	result := d.Detect(storage, 200)
	assert.Empty(t, result.Anomalies, "stable periodic signal must not fire")
}

// TestWasserstein_FiresOnLevelShift verifies the canonical positive case: a
// 60-point pre-shift block followed by a 60-point post-shift block at a
// 4-unit higher mean clears both gates on the first scoring tick.
//
// Plan note: the plan called for 80+80 points and "fires within 10 points of
// the shift", but the algorithm needs both windows full (refCount=curCount=60)
// before the first scoring tick can run, which is at point 120 — exactly when
// ref is fully pre-shift and cur is fully post-shift. We use 60+60 so the
// scoring tick lines up with the cleanest split, which is also the strictest
// test of the W1 computation.
func TestWasserstein_FiresOnLevelShift(t *testing.T) {
	d := testWassersteinDetector()
	storage := newTimeSeriesStorage()
	rng := rand.New(rand.NewSource(42)) //nolint:gosec // deterministic test seed

	ts := addWassersteinPoints(storage, 60, 1, func(_ int) float64 {
		return 10.0 + rng.NormFloat64()
	})
	ts = addWassersteinPoints(storage, 60, ts, func(_ int) float64 {
		return 14.0 + rng.NormFloat64()
	})

	result := d.Detect(storage, ts-1)
	require.Len(t, result.Anomalies, 1, "a clean 4-unit level shift must fire exactly once when both windows fill")

	a := result.Anomalies[0]
	assert.Equal(t, "wasserstein", a.DetectorName)
	assert.Contains(t, a.Title, "Wasserstein shift:")
	require.NotNil(t, a.Score, "anomaly must carry a score")
	assert.GreaterOrEqual(t, *a.Score, 4.0, "D-derived score should be at least DistanceThresholdMAD")
}

// TestWasserstein_FiresOnDistributionalShift exercises the dispersion-vs-rank
// novelty: a uniform reference window followed by an asymmetric bimodal cur
// window. The W1 distance is dominated by the spread of cur's mass off the
// median, not the median offset itself — though some median shift is required
// to clear the MinDeviationMAD effect-size gate.
//
// Plan note: the plan's test #4 asked for a SYMMETRIC bimodal cur with the
// SAME median as ref (uniform [9,11] vs bimodal {8.5, 11.5}, both median 10),
// asserting it fires "to differentiate Wasserstein from rank/mean tests." But
// the plan's algorithm includes a MinDeviationMAD=2.5 gate on
// |median(cur) - median(ref)|/MAD(ref); a perfectly symmetric bimodal with
// the same median has devMAD = 0 and would not fire under the plan's
// algorithm. We use an asymmetric bimodal (20×8 + 40×13) so the cur median
// shifts to 13 and the gate is cleared, while the bulk of the W1 signal
// comes from the spread of cur's mass — exercising the dispersion-aware
// quantile-difference summation that distinguishes W1 from a pure mean test.
func TestWasserstein_FiresOnDistributionalShift(t *testing.T) {
	d := testWassersteinDetector()
	storage := newTimeSeriesStorage()
	rng := rand.New(rand.NewSource(7)) //nolint:gosec // deterministic test seed

	// Reference: uniform on [9, 11] (median ≈ 10, MAD ≈ 0.5–0.6).
	ts := addWassersteinPoints(storage, 60, 1, func(_ int) float64 {
		return 9.0 + 2.0*rng.Float64()
	})
	// Current: asymmetric bimodal — 20 points around 6, then 40 around 14.
	// The asymmetric split puts position 30 in the 14-cluster, so medCur ≈ 14
	// and the MinDeviationMAD gate is comfortably cleared. The wide spread
	// between clusters drives the W1 mass-transport cost above 4·MAD —
	// exercising the order-statistic-difference summation.
	ts = addWassersteinPoints(storage, 20, ts, func(_ int) float64 {
		return 6.0 + 0.05*rng.NormFloat64()
	})
	ts = addWassersteinPoints(storage, 40, ts, func(_ int) float64 {
		return 14.0 + 0.05*rng.NormFloat64()
	})

	result := d.Detect(storage, ts-1)
	require.NotEmpty(t, result.Anomalies, "uniform→bimodal distributional shift must fire")
	// Cooldown=30 + only 60 post-fill points means at most one fire is possible
	// here; tighten the assertion.
	assert.LessOrEqual(t, len(result.Anomalies), 1, "cooldown must bound emission to 1 within the test window")

	a := result.Anomalies[0]
	require.NotNil(t, a.Score)
	assert.GreaterOrEqual(t, *a.Score, 4.0, "D-derived score must clear DistanceThresholdMAD")
}

// TestWasserstein_NoFireOnSlowDrift verifies the detector ignores slow linear
// drift. Mannkendall is the right tool for that pattern; Wasserstein should
// stay quiet because the median offset between adjacent 60-point windows is
// small in MAD units. This guards against W1 over-firing on the long-but-tiny
// drift territory that mannkendall is designed for.
func TestWasserstein_NoFireOnSlowDrift(t *testing.T) {
	d := testWassersteinDetector()
	storage := newTimeSeriesStorage()
	rng := rand.New(rand.NewSource(13)) //nolint:gosec // deterministic test seed

	// 200 points with slope = 0.005/point ⇒ total drift = 1.0. Adjacent
	// 60-point windows are offset by ~0.3 in mean, with σ=1 noise the
	// MAD-normalized devMAD is ≈ 0.3/0.67 ≈ 0.45 — well below
	// MinDeviationMAD=2.5.
	addWassersteinPoints(storage, 200, 1, func(i int) float64 {
		return 10.0 + 0.005*float64(i) + rng.NormFloat64()
	})

	result := d.Detect(storage, 200)
	assert.Empty(t, result.Anomalies,
		"slow linear drift is mannkendall territory — Wasserstein must not double-fire on it")
}

// TestWasserstein_Cooldown verifies that two shifts close together produce
// only one anomaly. After the first fire, CooldownPoints=30 must suppress
// re-evaluation through the second shift's onset.
func TestWasserstein_Cooldown(t *testing.T) {
	d := testWassersteinDetector()
	storage := newTimeSeriesStorage()
	rng := rand.New(rand.NewSource(101)) //nolint:gosec // deterministic test seed

	// Phase 1: 60 stable points at mean=10.
	ts := addWassersteinPoints(storage, 60, 1, func(_ int) float64 {
		return 10.0 + rng.NormFloat64()*0.3
	})
	// Phase 2: 20 points at mean=14 (first shift, fills cur; first scoring
	// tick at point 120 will see this).
	ts = addWassersteinPoints(storage, 20, ts, func(_ int) float64 {
		return 14.0 + rng.NormFloat64()*0.3
	})
	// Phase 3: 60 points at mean=18 (second shift); enters cur immediately
	// after phase 2. By point 120 cur contains a mix dominated by 18.
	ts = addWassersteinPoints(storage, 60, ts, func(_ int) float64 {
		return 18.0 + rng.NormFloat64()*0.3
	})

	result := d.Detect(storage, ts-1)
	require.Len(t, result.Anomalies, 1,
		"two shifts within 20 points must collapse to a single fire (cooldown=30)")
}

// TestWasserstein_RemoveSeriesClearsState verifies the SeriesRemover contract:
// per-(ref, agg) state is dropped when storage frees a ref, so the detector
// state map cannot grow unbounded with cumulative series cardinality. Other
// series' state must be untouched.
func TestWasserstein_RemoveSeriesClearsState(t *testing.T) {
	d := NewWassersteinDetector() // default Aggregations: [Average, Count]
	storage := newTimeSeriesStorage()

	// Register 2 distinct series with enough points to exist in the detector's
	// per-series map (any Detect call instantiates state for every listed
	// series, even if no points have been ingested yet).
	for s := 0; s < 2; s++ {
		name := "metric" + string(rune('A'+s))
		for i := 0; i < 5; i++ {
			storage.Add("ns", name, float64(i), int64(i+1), nil)
		}
	}

	d.Detect(storage, 5)
	require.Len(t, d.series, 2*len(d.Aggregations),
		"each series should have one state entry per aggregation")

	metas := storage.ListSeries(observer.WorkloadSeriesFilter())
	require.Len(t, metas, 2)
	survivor := metas[1].Ref

	d.RemoveSeries([]observer.SeriesRef{metas[0].Ref})

	// Only state for the surviving series should remain — and only for that
	// ref·agg pair.
	assert.Len(t, d.series, len(d.Aggregations),
		"only state for the surviving series should remain after RemoveSeries")
	for k := range d.series {
		assert.Equal(t, survivor, k.ref, "remaining state must belong to the surviving ref")
	}
	assert.Nil(t, d.cachedSeries, "RemoveSeries must invalidate the cached series list")
}

// TestWasserstein_NoNewDataNoWork verifies the replay-skip cursor: a Detect
// call with no new points and no in-place writes returns no anomalies and
// must not double-process the existing windows.
func TestWasserstein_NoNewDataNoWork(t *testing.T) {
	d := testWassersteinDetector()
	storage := newTimeSeriesStorage()
	rng := rand.New(rand.NewSource(42)) //nolint:gosec // deterministic test seed

	ts := addWassersteinPoints(storage, 60, 1, func(_ int) float64 {
		return 10.0 + rng.NormFloat64()
	})
	ts = addWassersteinPoints(storage, 60, ts, func(_ int) float64 {
		return 14.0 + rng.NormFloat64()
	})

	r1 := d.Detect(storage, ts-1)
	require.Len(t, r1.Anomalies, 1)

	// Second call with no new data should be a no-op and must not refire even
	// if cooldown is zero, because nothing new was ingested.
	r2 := d.Detect(storage, ts-1)
	assert.Empty(t, r2.Anomalies, "no new data must produce no anomalies")
}

// TestWasserstein_Reset verifies Reset clears the per-series map and the
// cached series list, mirroring the contract on MannKendall/ScanMW.
func TestWasserstein_Reset(t *testing.T) {
	d := testWassersteinDetector()
	storage := newTimeSeriesStorage()

	for i := 0; i < 60; i++ {
		storage.Add("ns", "metric", float64(i), int64(i+1), nil)
	}
	d.Detect(storage, 60)
	assert.NotEmpty(t, d.series, "should have state after detection")

	d.Reset()
	assert.Empty(t, d.series, "reset should clear all state")
	assert.Nil(t, d.cachedSeries, "reset should clear cached series")
}
