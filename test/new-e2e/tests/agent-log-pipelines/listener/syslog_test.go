func (s *dockerSyslogTCPSuite) TestSyslogStructuredOutput() {
	assertSyslogStructuredOutput(s.T(), s.EventuallyWithT, s.Env(), syslogSendOpts{
		cmd: []string{"sh", "-c",
			// -w 1: BusyBox nc won't exit after stdin EOF because the agent
			// holds the TCP connection open; the timeout ensures nc exits.
			`printf '<134>1 2025-06-15T10:30:00Z testhost testapp 1234 - - Syslog e2e test message\n' | nc -w 1 agent 10514`,
		},
	})
}

func (s *dockerSyslogUDPSuite) TestSyslogStructuredOutput() {
	assertSyslogStructuredOutput(s.T(), s.EventuallyWithT, s.Env(), syslogSendOpts{
		cmd: []string{"sh", "-c",
			// -u: send via UDP.  -w 1: exit after 1 s (no response expected).
			// No trailing newline: a UDP syslog datagram is delimited by the
			// packet boundary (RFC 5426), not by a newline as in TCP framing.
			// Sending one would leave a stray '\n' in the parsed message body.
			`printf '<134>1 2025-06-15T10:30:00Z testhost testapp 1234 - - Syslog e2e test message' | nc -u -w 1 agent 10515`,
		},
		// UDP is connectionless — nc -u always exits 0 even if the listener
		// isn't ready, so the separate send-retry loop is ineffective.
		// Re-send on every verification tick to handle the startup race.
		resendEachTick: true,
	})
}

func (s *dockerSyslogTLSSuite) TestSyslogStructuredOutput() {
	assertSyslogStructuredOutput(s.T(), s.EventuallyWithT, s.Env(), syslogSendOpts{
		cmd: []string{"sh", "-c",
			// openssl s_client connects over TLS; -CAfile trusts the self-signed
			// cert generated by the init container. -quiet suppresses the
			// handshake banner so only the syslog payload is sent.
			// -quiet implies -ign_eof, which makes s_client ignore the printf
			// EOF and block until the agent closes the connection (it won't),
			// so -no_ign_eof restores exit-on-EOF and `timeout` is a hard
			// backstop in case the handshake stalls.
			`printf '<134>1 2025-06-15T10:30:00Z testhost testapp 1234 - - Syslog e2e test message\n' | timeout 10 openssl s_client -connect agent:10516 -CAfile /certs/server.crt -quiet -no_ign_eof 2>/dev/null`,
		},
	})
}

type syslogSendOpts struct {
	cmd []string
	// resendEachTick causes the message to be re-sent on every verification
	// tick. Use for UDP where the initial send may be silently dropped
	// because nc -u always exits 0 even when the listener isn't ready.
	resendEachTick bool
}

// assertSyslogStructuredOutput sends an RFC 5424 syslog message and verifies
// the agent parses it into structured JSON with the expected syslog fields.
// PRI 134 = facility 16 (local0) * 8 + severity 6 (info).
func assertSyslogStructuredOutput(
	t *testing.T,
	eventuallyWithT func(condition func(collect *assert.CollectT), waitFor time.Duration, tick time.Duration, msgAndArgs ...interface{}) bool,
	env *environments.DockerHost,
	opts syslogSendOpts,
) {
	t.Helper()

	eventuallyWithT(func(c *assert.CollectT) {
		agentReady := env.Agent.Client.IsReady()
		assert.True(c, agentReady)
	}, 1*time.Minute, 5*time.Second, "Agent was not ready")

	t.Logf("Testing Agent Version '%v'", env.Agent.Client.Version())

	if !opts.resendEachTick {
		// For connection-oriented transports (TCP, TLS), the send fails
		// visibly if the listener isn't ready, so a retry loop suffices.
		eventuallyWithT(func(c *assert.CollectT) {
			stdout, stderr, err := env.Docker.Client.ExecuteCommandStdoutStdErr("syslog-sender", opts.cmd...)
			assert.NoError(c, err, "failed to send syslog message: stdout=%s stderr=%s", stdout, stderr)
		}, 1*time.Minute, 5*time.Second, "syslog send never succeeded")
	}

	eventuallyWithT(func(c *assert.CollectT) {
		if opts.resendEachTick {
			// Re-send every tick — UDP datagrams may be silently dropped
			// if the listener isn't ready yet.
			env.Docker.Client.ExecuteCommandStdoutStdErr("syslog-sender", opts.cmd...) //nolint:errcheck
		}

		logs, err := utils.FetchAndFilterLogs(env.FakeIntake, "syslog-e2e", "Syslog e2e test message")
		if !assert.NoError(c, err) {
			return
		}
		if !assert.NotEmpty(c, logs, "no logs matching 'Syslog e2e test message' found") {
			return
		}
		t.Logf("matched %d logs in fakeintake for 'Syslog e2e test message'", len(logs))

		log := logs[0]

		var body map[string]interface{}
		if !assert.NoError(c, json.Unmarshal([]byte(log.Message), &body), "log.Message is not valid JSON: %s", log.Message) {
			return
		}

		// message is intentionally the full raw syslog frame: debug_attr_parsing
		// only appends syslog/siem keys alongside it, it does not replace the
		// transmitted content with the parsed MSG body. The framer trims the
		// trailing newline, so all transports land on the same value.
		assert.Equal(c, "<134>1 2025-06-15T10:30:00Z testhost testapp 1234 - - Syslog e2e test message", body["message"], "message should be the full raw syslog line")
		if !assert.Contains(c, body, "syslog", "missing 'syslog' key in structured output") {
			return
		}

		syslogMap, ok := body["syslog"].(map[string]interface{})
		if !assert.True(c, ok, "'syslog' value is not a map: %T", body["syslog"]) {
			return
		}
		assert.Equal(c, "testapp", syslogMap["appname"], "unexpected appname")
		assert.EqualValues(c, 6, syslogMap["severity"], "unexpected severity")
		assert.EqualValues(c, 16, syslogMap["facility"], "unexpected facility")

		assert.Equal(c, "info", log.Status, "unexpected log status")
		assert.Equal(c, "syslog-e2e", log.Source, "unexpected log source")
	}, 2*time.Minute, 10*time.Second)
}
