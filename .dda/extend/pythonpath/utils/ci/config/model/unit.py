from __future__ import annotations

from typing import Annotated, Literal

from msgspec import Meta, Struct, field

# TODO: use a metaclass when we upgrade msgspec to 0.20.0
default_settings = {
    "forbid_unknown_fields": True,
    "frozen": True,
    "kw_only": True,
    "omit_defaults": True,
}


class CIGlobalTriggerConfig(Struct, **default_settings):
    """
    A trigger is a set of changes that will cause a unit to run.
    """

    allow_manual: Annotated[
        bool,
        Meta(description="Whether the unit can be triggered manually via the `TRIGGER_UNITS` environment variable"),
    ] = True


class CITrigger(Struct, **default_settings):
    """
    A trigger is a set of changes that will cause a unit to run.
    """

    include: Annotated[
        Annotated[list[Annotated[str, Meta(min_length=1)]], Meta(min_length=1)] | None,
        Meta(description="Glob patterns relative to the repository root that will trigger the unit"),
    ] = None
    exclude: Annotated[
        Annotated[list[Annotated[str, Meta(min_length=1)]], Meta(min_length=1)] | None,
        Meta(
            description=(
                "Glob patterns relative to the repository root that will exclude the unit from running, "
                "taking precedence over `include`"
            ),
        ),
    ] = None

    def __post_init__(self) -> None:
        for field_name, patterns in (("include", self.include), ("exclude", self.exclude)):
            if patterns is None:
                continue

            patterns_to_indices = {}
            for i, pattern in enumerate(patterns):
                patterns_to_indices.setdefault(pattern, []).append(i)

            errors = []
            for pattern, indices in patterns_to_indices.items():
                if len(indices) > 1:
                    msg = (
                        f"Duplicate `{field_name}` pattern `{pattern}` at indices "
                        f"{', '.join(str(i) for i in indices)}"
                    )
                    errors.append(msg)

            if errors:
                raise ValueError("\n".join(errors))


class CIPipeline(Struct, tag_field="type", **default_settings):
    provider: Annotated[
        Literal["gitlab"],
        Meta(description="The intended scheduler of the pipeline"),
    ] = "gitlab"


class CIStaticPipeline(CIPipeline, tag="static", **default_settings):
    """
    A static pipeline is defined in a file.
    """

    path: Annotated[
        str,
        Meta(
            min_length=1,
            description="The path to the pipeline YAML file relative to the repository root",
        ),
    ]


class CIDynamicPipeline(CIPipeline, tag="dynamic", **default_settings):
    """
    A dynamic pipeline is generated by a command.
    """

    command: Annotated[
        str,
        Meta(
            min_length=1,
            description="The command that outputs the pipeline YAML file contents e.g. `cat pipeline.yml`",
        ),
    ]


class CIUnit(Struct, **default_settings):
    """
    A CI unit is a collection of jobs that may run independently.
    """

    name: Annotated[
        str,
        Meta(
            min_length=1,
            description=(
                "A human-readable name for the unit that is available at runtime as the "
                "`UNIT_DISPLAY_NAME` environment variable"
            ),
        ),
    ]
    description: Annotated[
        str,
        Meta(
            min_length=1,
            description="A short description of the unit that is used for documentation purposes",
        ),
    ]
    trigger: Annotated[
        CIGlobalTriggerConfig,
        Meta(
            description="The global trigger configuration for the unit",
        ),
    ] = field(default_factory=CIGlobalTriggerConfig)
    pipeline: Annotated[
        CIDynamicPipeline | CIStaticPipeline,
        Meta(description="The pipeline configuration, whose schema depends on the `type` field e.g. dynamic or static"),
    ]
    triggers: Annotated[
        list[CITrigger],
        Meta(
            min_length=1,
            description="The triggers that cause the unit to run",
        ),
    ]

    def __post_init__(self) -> None:
        # Ensure that at least one trigger defines an `include` with more than one pattern
        if not any(trigger.include and len(trigger.include) > 1 for trigger in self.triggers):
            msg = "At least one trigger must define an `include` with at least two patterns"
            raise ValueError(msg)
