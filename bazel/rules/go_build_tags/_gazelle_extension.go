package go_build_tags

import (
	"bufio"
	"go/build/constraint"
	"log"
	"os"
	"path/filepath"
	"strings"

	"github.com/bazelbuild/bazel-gazelle/language"
	"github.com/bazelbuild/bazel-gazelle/rule"
)

const name = "go_build_tags"

type lang struct {
	language.BaseLang
}

func NewLanguage() language.Language {
	return &lang{}
}

func (*lang) Name() string                    { return name }
func (*lang) Kinds() map[string]rule.KindInfo { return nil }
func (*lang) KnownDirectives() []string       { return nil }
func (*lang) Loads() []rule.LoadInfo          { return nil }

// GenerateRules handles two categories of non-_test.go files that the Go
// language extension misplaces when custom tags are listed in -build_tags:
//
//  1. Stub files: Gazelle evaluates their constraint as false (because the
//     relevant custom tags are listed in -build_tags and thus always-true).
//     This extension adds them back to go_library.srcs so rules_go can select
//     the right variant based on //go/config:tags at build time.
//
//  2. Test-only sources: files with a build constraint that evaluates to true
//     only when the "test" tag is active. Because "test" is intentionally
//     absent from -build_tags, Gazelle excludes them from go_library.srcs.
//     This extension moves them into go_test.srcs where they belong.
//
// Both cases modify the rules generated by the Go extension in-place via
// OtherGen; no new rules are emitted.
func (*lang) GenerateRules(args language.GenerateArgs) language.GenerateResult {
	stubs, testSrcs := classifyFiles(args.Dir, args.RegularFiles)
	if len(stubs) == 0 && len(testSrcs) == 0 {
		return language.GenerateResult{}
	}

	for _, r := range args.OtherGen {
		switch r.Kind() {
		case "go_library":
			r.SetAttr("srcs", append(r.AttrStrings("srcs"), stubs...))
		case "go_test":
			r.SetAttr("srcs", append(r.AttrStrings("srcs"), testSrcs...))
		}
	}
	return language.GenerateResult{}
}

// classifyFiles inspects each non-_test.go Go file in regularFiles and
// returns two groups:
//   - stubs: files excluded by Gazelle because their constraint is false when
//     all known custom tags are active (regardless of the "test" tag)
//   - testSrcs: files whose constraint becomes true only when "test" is active
func classifyFiles(dir string, regularFiles []string) (stubs, testSrcs []string) {
	for _, f := range regularFiles {
		if !strings.HasSuffix(f, ".go") || strings.HasSuffix(f, "_test.go") {
			continue
		}
		expr := parseConstraint(filepath.Join(dir, f))
		if expr == nil {
			continue
		}
		// Evaluate twice: once as Gazelle sees it (test=false, everything else
		// true), and once with test=true.
		withoutTest := expr.Eval(func(tag string) bool { return tag != "test" })
		withTest := expr.Eval(func(tag string) bool { return true })

		switch {
		case !withoutTest && withTest:
			// test=true activates the file → belongs in go_test.
			testSrcs = append(testSrcs, f)
		case !withoutTest && !withTest:
			// Excluded regardless of test → stub excluded due to known custom
			// tags; add back to go_library so rules_go can select correctly.
			stubs = append(stubs, f)
		case withoutTest && !withTest:
			// Gazelle includes it but test=true would exclude it: !test constraint.
			log.Printf("warning: %s has a //go:build !test constraint, which is likely a mistake", f)
		}
	}
	return
}

// parseConstraint returns the build constraint expression for the given file,
// or nil if the file has no constraint or cannot be read.
func parseConstraint(path string) constraint.Expr {
	f, err := os.Open(path)
	if err != nil {
		return nil
	}
	defer f.Close()

	scanner := bufio.NewScanner(f)
	for scanner.Scan() {
		line := strings.TrimSpace(scanner.Text())
		if line == "" {
			continue
		}
		if !strings.HasPrefix(line, "//") {
			return nil
		}
		expr, err := constraint.Parse(line)
		if err != nil {
			// Regular // comment (e.g. license header) — keep scanning.
			continue
		}
		return expr
	}
	return nil
}
