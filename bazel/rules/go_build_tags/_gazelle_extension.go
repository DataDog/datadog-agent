const extName = "go_build_tags"

// go_testutil is our custom Bazel rule kind, a macro wrapping go_library(testonly=True).
// Using a dedicated kind lets this extension own the rule in Gazelle's merge logic,
// preventing the Go extension from deleting it.
const goTestutilKind = "go_testutil"

type lang struct {
	language.BaseLang
}

func NewLanguage() language.Language {
	return &lang{}
}

func (*lang) Name() string { return extName }

func (*lang) Kinds() map[string]rule.KindInfo {
	return map[string]rule.KindInfo{
		goTestutilKind: {
			NonEmptyAttrs:  map[string]bool{"srcs": true},
			MergeableAttrs: map[string]bool{"srcs": true, "embed": true, "deps": true},
		},
	}
}

func (*lang) Loads() []rule.LoadInfo {
	return []rule.LoadInfo{
		{
			Name:    "//bazel/rules/go_build_tags:defs.bzl",
			Symbols: []string{goTestutilKind},
		},
	}
}

// GenerateRules handles non-_test.go files with a //go:build test constraint.
//
// Because "test" is in Gazelle's build_tags, the Go extension includes them in
// go_library.srcs. This extension moves those files out of go_library and into
// a dedicated testonly go_testutil named {lib}_testutil. The testutil always
// embeds the main library so it inherits the package's importpath, enabling
// same-package tests to embed it and cross-package tests to depend on it.
//
// The same-package go_test has its embed updated from :{lib} to :{lib}_testutil
// so the test-gated files are part of the compiled package.
//
// If the package has no go_test target, the _testutil library is still generated
// so that other packages can consume it.
func (*lang) GenerateRules(args language.GenerateArgs) language.GenerateResult {
	// Operate on the files already in go_library.srcs rather than the raw
	// filesystem listing. Files that the Go extension excluded (e.g. because
	// another configured tag conflicts with the constraint) should not be moved.
	libName, libSrcs := libraryInfo(args.OtherGen)
	if libName == "" {
		return language.GenerateResult{}
	}
	testSrcs := listTestSrcs(args.Dir, args.RegularFiles, libSrcs)
	if len(testSrcs) == 0 {
		return language.GenerateResult{}
	}
	testutilName := libName + "_testutil"

	testSrcSet := make(map[string]struct{}, len(testSrcs))
	for _, s := range testSrcs {
		testSrcSet[s] = struct{}{}
	}

	for _, r := range args.OtherGen {
		switch r.Kind() {
		case "go_library":
			srcs := r.AttrStrings("srcs")
			filtered := make([]string, 0, len(srcs))
			for _, s := range srcs {
				if _, ok := testSrcSet[s]; !ok {
					filtered = append(filtered, s)
				}
			}
			if len(filtered) != len(srcs) {
				r.SetAttr("srcs", filtered)
			}
		case "go_test":
			// Always embed the testutil: the go_test needs it to compile
			// test-gated files in the same package. We set unconditionally
			// (without reading first) because AttrStrings returns an empty
			// slice for rules generated by the Go extension.
			r.SetAttr("embed", []string{":" + testutilName})
			addStringToListIfMissing(r, "gotags", "test")
		}
	}

	// Collect non-stdlib imports from the test-gated files for dep resolution.
	testImports := parseGoImports(args.Dir, testSrcs)

	testutil := rule.NewRule(goTestutilKind, testutilName)
	testutil.SetAttr("srcs", testSrcs)
	testutil.SetAttr("embed", []string{":" + libName})
	testutil.SetAttr("visibility", []string{"//visibility:public"})

	return language.GenerateResult{
		Gen:     []*rule.Rule{testutil},
		Imports: []interface{}{testImports},
	}
}

// Imports returns the ImportSpecs for this go_testutil rule.
//
// We return the embedded library's importpath under our own language name
// ("go_build_tags"). This lets our Resolve() find testutil rules via
// ix.FindRulesByImportWithConfig with lang="go_build_tags", bypassing the
// lang filter that would suppress them in standard "go" queries.
//
// A non-nil return also ensures the rule is indexed; the embedded library's
// importpaths are propagated via Embeds() to enable cross-language lookups.
func (*lang) Imports(_ *config.Config, r *rule.Rule, f *rule.File) []resolve.ImportSpec {
	if r.Kind() != goTestutilKind {
		return nil
	}
	var specs []resolve.ImportSpec
	for _, embed := range r.AttrStrings("embed") {
		if !strings.HasPrefix(embed, ":") {
			continue // only same-package embeds are resolvable here
		}
		targetName := embed[1:]
		for _, fr := range f.Rules {
			if fr.Name() == targetName {
				if imp := fr.AttrString("importpath"); imp != "" {
					specs = append(specs, resolve.ImportSpec{Lang: extName, Imp: imp})
				}
				break
			}
		}
	}
	if specs == nil {
		specs = []resolve.ImportSpec{} // empty (not nil) to still be indexed
	}
	return specs
}

// Embeds returns the label of the library embedded by a go_testutil rule.
// This lets the rule index propagate the embedded library's importpaths to the
// testutil, enabling cross-package testonly-preference resolution.
func (*lang) Embeds(r *rule.Rule, from label.Label) []label.Label {
	if r.Kind() != goTestutilKind {
		return nil
	}
	embeds := r.AttrStrings("embed")
	labels := make([]label.Label, 0, len(embeds))
	for _, e := range embeds {
		lbl, err := label.Parse(e)
		if err != nil {
			continue
		}
		if lbl.Relative {
			lbl.Repo = from.Repo
			lbl.Pkg = from.Pkg
		}
		labels = append(labels, lbl)
	}
	return labels
}

// Resolve sets the deps attribute of a go_testutil rule by mapping the Go
// importpaths collected during GenerateRules to Bazel labels.
//
// For each import, we first search the go_build_tags index (which only contains
// go_testutil rules) to prefer a testonly provider. This is needed because
// FindRulesByImport filters by record.Lang, and go_testutil records have
// Lang="go_build_tags", invisible to standard "go" queries. If no testutil
// provider is found, we fall back to the standard "go" lookup.
func (*lang) Resolve(c *config.Config, ix *resolve.RuleIndex, _ *repo.RemoteCache, r *rule.Rule, imports interface{}, from label.Label) {
	if r.Kind() != goTestutilKind {
		return
	}
	goImports, ok := imports.([]string)
	if !ok || len(goImports) == 0 {
		return
	}

	var deps []string
	for _, imp := range goImports {
		if isStdlibImport(imp) {
			continue
		}
		// Try testutil providers first (indexed under our own language name).
		if results := ix.FindRulesByImportWithConfig(c, resolve.ImportSpec{Lang: extName, Imp: imp}, extName); len(results) > 0 {
			best := results[0]
			if !best.IsSelfImport(from) {
				deps = append(deps, formatLabel(best.Label, from))
				continue
			}
		}
		// Fall back to standard Go resolution.
		results := ix.FindRulesByImportWithConfig(c, resolve.ImportSpec{Lang: "go", Imp: imp}, "go")
		if len(results) == 0 {
			continue
		}
		best := results[0]
		if best.IsSelfImport(from) {
			continue
		}
		deps = append(deps, formatLabel(best.Label, from))
	}
	if len(deps) > 0 {
		r.SetAttr("deps", deps)
	}
}

// formatLabel formats a label relative to from's package and repo.
func formatLabel(lbl label.Label, from label.Label) string {
	if lbl.Repo == from.Repo || lbl.Repo == "" {
		lbl.Repo = ""
	}
	if lbl.Pkg == from.Pkg {
		return ":" + lbl.Name
	}
	return lbl.String()
}

// parseGoImports returns the non-stdlib Go importpaths from all given files.
func parseGoImports(dir string, files []string) []string {
	seen := make(map[string]struct{})
	var imports []string
	for _, f := range files {
		fset := token.NewFileSet()
		af, err := parser.ParseFile(fset, filepath.Join(dir, f), nil, parser.ImportsOnly)
		if err != nil {
			continue
		}
		for _, imp := range af.Imports {
			path := strings.Trim(imp.Path.Value, `"`)
			if isStdlibImport(path) {
				continue
			}
			if _, ok := seen[path]; !ok {
				seen[path] = struct{}{}
				imports = append(imports, path)
			}
		}
	}
	return imports
}

// isStdlibImport reports whether path is a Go standard library import.
// Standard library paths have no dot in their first path component.
func isStdlibImport(path string) bool {
	first := path
	if i := strings.IndexByte(path, '/'); i >= 0 {
		first = path[:i]
	}
	return !strings.ContainsRune(first, '.')
}

// libraryInfo returns the name and source-file set of the first go_library rule
// found in rules. It returns ("", nil) if no go_library is present.
func libraryInfo(rules []*rule.Rule) (name string, srcs map[string]struct{}) {
	for _, r := range rules {
		if r.Kind() == "go_library" {
			sl := r.AttrStrings("srcs")
			set := make(map[string]struct{}, len(sl))
			for _, s := range sl {
				set[s] = struct{}{}
			}
			return r.Name(), set
		}
	}
	return "", nil
}

// addStringToListIfMissing appends value to the named string-list attribute of r
// if it is not already present.
func addStringToListIfMissing(r *rule.Rule, attr, value string) {
	for _, s := range r.AttrStrings(attr) {
		if s == value {
			return
		}
	}
	r.SetAttr(attr, append(r.AttrStrings(attr), value))
}

// listTestSrcs returns the non-_test.go Go files from regularFiles that are
// present in libSrcs and whose build constraint positively requires the "test"
// tag.
//
// A file "requires test" when (1) its constraint is false with test=false and
// all other tags true, AND (2) its constraint is true with test=true and all
// other tags true. Both conditions are needed: without (2), a file like
// "!seclmax && !test" would be incorrectly classified as test-gated, because
// setting seclmax=true (part of the "maximize all tags" assignment) makes its
// constraint false even though the test tag is irrelevant.
func listTestSrcs(dir string, regularFiles []string, libSrcs map[string]struct{}) (testSrcs []string) {
	for _, f := range regularFiles {
		if !strings.HasSuffix(f, ".go") || strings.HasSuffix(f, "_test.go") {
			continue
		}
		if _, ok := libSrcs[f]; !ok {
			continue
		}
		expr := parseConstraint(filepath.Join(dir, f))
		if expr == nil {
			continue
		}
		// A file requires the "test" tag if and only if:
		//   1. The constraint is false when test=false (all other tags true), AND
		//   2. The constraint is true when test=true (all tags true).
		// Check 2 is necessary to reject files like "!seclmax && !test": the
		// "maximize all tags" assignment used in check 1 sets seclmax=true, making
		// the expression false even though the file is not test-gated, it just
		// excludes seclmax builds.
		withoutTest := expr.Eval(func(tag string) bool { return tag != "test" })
		if withoutTest {
			continue
		}
		withTest := expr.Eval(func(tag string) bool { return true })
		if withTest {
			testSrcs = append(testSrcs, f)
		}
	}
	return
}

// parseConstraint returns the build constraint expression for the given file,
// or nil if the file has no constraint or cannot be read.
func parseConstraint(path string) constraint.Expr {
	f, err := os.Open(path)
	if err != nil {
		return nil
	}
	defer f.Close()

	scanner := bufio.NewScanner(f)
	for scanner.Scan() {
		line := strings.TrimSpace(scanner.Text())
		if line == "" {
			continue
		}
		if !strings.HasPrefix(line, "//") {
			return nil
		}
		expr, err := constraint.Parse(line)
		if err != nil {
			// Regular // comment, keep scanning.
			continue
		}
		return expr
	}
	return nil
}
