package go_build_tags

import (
	"bufio"
	"go/build/constraint"
	"os"
	"path/filepath"
	"strings"

	"github.com/bazelbuild/bazel-gazelle/language"
	"github.com/bazelbuild/bazel-gazelle/rule"
)

const name = "go_build_tags"

type lang struct {
	language.BaseLang
}

func NewLanguage() language.Language {
	return &lang{}
}

func (*lang) Name() string { return name }

// GenerateRules handles non-_test.go files with a //go:build test constraint.
// Because "test" is in -build_tags, Gazelle includes them in go_library.srcs.
// This extension moves those files out of go_library and into go_test.
//
// This modifies the rules generated by the Go extension in-place via OtherGen;
// no new rules are emitted.
func (*lang) GenerateRules(args language.GenerateArgs) language.GenerateResult {
	// Operate on the files already in go_library.srcs rather than the raw
	// filesystem listing. Files that the Go extension excluded (e.g. because
	// another configured tag conflicts with the constraint) should not be moved.
	libSrcs := librarySourceSet(args.OtherGen)
	testSrcs := classifyFiles(args.Dir, args.RegularFiles, libSrcs)
	if len(testSrcs) == 0 {
		return language.GenerateResult{}
	}

	// Only remove test-only files from go_library when there is a go_test
	// target to receive them. If there is none (e.g. pure helper packages),
	// leave them in go_library: downstream tests that embed the library with
	// gotags = ["test"] will recompile all its srcs, including the helpers.
	removeFromLibrary := hasTestTarget(args.OtherGen)

	testSrcSet := make(map[string]bool, len(testSrcs))
	for _, s := range testSrcs {
		testSrcSet[s] = true
	}

	for _, r := range args.OtherGen {
		switch r.Kind() {
		case "go_library":
			if !removeFromLibrary {
				continue
			}
			srcs := r.AttrStrings("srcs")
			filtered := make([]string, 0, len(srcs))
			for _, s := range srcs {
				if !testSrcSet[s] {
					filtered = append(filtered, s)
				}
			}
			if len(filtered) != len(srcs) {
				r.SetAttr("srcs", filtered)
			}
		case "go_test":
			r.SetAttr("srcs", append(r.AttrStrings("srcs"), testSrcs...))
			addStringToListIfMissing(r, "gotags", "test")
		}
	}
	return language.GenerateResult{}
}

// librarySourceSet returns the set of filenames listed in the first go_library
// rule found in rules, or nil if there is none.
func librarySourceSet(rules []*rule.Rule) map[string]bool {
	for _, r := range rules {
		if r.Kind() == "go_library" {
			srcs := r.AttrStrings("srcs")
			set := make(map[string]bool, len(srcs))
			for _, s := range srcs {
				set[s] = true
			}
			return set
		}
	}
	return nil
}

// hasTestTarget reports whether any go_test rule is present in rules.
func hasTestTarget(rules []*rule.Rule) bool {
	for _, r := range rules {
		if r.Kind() == "go_test" {
			return true
		}
	}
	return false
}

// addStringToListIfMissing appends value to the named string-list attribute of r
// if it is not already present.
func addStringToListIfMissing(r *rule.Rule, attr, value string) {
	for _, s := range r.AttrStrings(attr) {
		if s == value {
			return
		}
	}
	r.SetAttr(attr, append(r.AttrStrings(attr), value))
}

// classifyFiles returns the non-_test.go Go files from regularFiles that are
// present in libSrcs and whose build constraint requires the "test" tag.
//
// A file "requires test" when its constraint evaluates to false with test=false
// regardless of all other tags. This is checked by evaluating the expression
// with every non-test tag set to true: if even the maximally-satisfied
// assignment still gives false without test, test is necessary.
func classifyFiles(dir string, regularFiles []string, libSrcs map[string]bool) (testSrcs []string) {
	for _, f := range regularFiles {
		if !strings.HasSuffix(f, ".go") || strings.HasSuffix(f, "_test.go") {
			continue
		}
		if !libSrcs[f] {
			continue
		}
		expr := parseConstraint(filepath.Join(dir, f))
		if expr == nil {
			continue
		}
		// Evaluate with all non-test tags true. If the expression is still
		// false, the "test" tag is the only way to satisfy it.
		withoutTest := expr.Eval(func(tag string) bool { return tag != "test" })
		if !withoutTest {
			testSrcs = append(testSrcs, f)
		}
	}
	return
}

// parseConstraint returns the build constraint expression for the given file,
// or nil if the file has no constraint or cannot be read.
func parseConstraint(path string) constraint.Expr {
	f, err := os.Open(path)
	if err != nil {
		return nil
	}
	defer f.Close()

	scanner := bufio.NewScanner(f)
	for scanner.Scan() {
		line := strings.TrimSpace(scanner.Text())
		if line == "" {
			continue
		}
		if !strings.HasPrefix(line, "//") {
			return nil
		}
		expr, err := constraint.Parse(line)
		if err != nil {
			// Regular // comment, keep scanning.
			continue
		}
		return expr
	}
	return nil
}
